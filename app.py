def export_excel():
    """Export monthly entries as an Excel (.xlsx) download."""
    db = get_db()
    today = date.today()

    # Get month/year from query params, default to current month
    try:
        month = int(request.args.get('month', today.month))
        year = int(request.args.get('year', today.year))
        if not (1 <= month <= 12):
            raise ValueError
    except (ValueError, TypeError):
        month = today.month
        year = today.year

    month_str = str(month).zfill(2)
    year_str = str(year)

    # Fetch all non-deleted entries for the month
    rows = db.execute('''
        SELECT date, type, description, amount, notes
        FROM daily_entries
        WHERE strftime('%m', date) = ? AND strftime('%Y', date) = ?
              AND is_deleted = 0
        ORDER BY date ASC, id ASC
    ''', (month_str, year_str)).fetchall()

    # Build data for DataFrame
    data = []
    for row in rows:
        data.append({
            'Date': row['date'],
            'Type': row['type'].capitalize(),
            'Description': row['description'],
            'Amount': row['amount'],
            'Notes': row['notes'] or '',
        })

    df = pd.DataFrame(data, columns=['Date', 'Type', 'Description', 'Amount', 'Notes'])

    # Write to an in-memory Excel file
    output = io.BytesIO()
    with pd.ExcelWriter(output, engine='openpyxl') as writer:
        df.to_excel(writer, index=False, sheet_name='Entries')
    output.seek(0)

    # Generate filename like "WaqarBrothers_Apr_2026.xlsx"
    month_name = date(year, month, 1).strftime('%b')
    filename = f'WaqarBrothers_{month_name}_{year}.xlsx'

    return send_file(output,
                     as_attachment=True,
                     download_name=filename,
                     mimetype='application/vnd.openxmlformats-officedocument.spreadsheetml.sheet')


