@login_required
def edit_entry(entry_id):
    """Show edit form (GET) or save edited entry (POST)."""
    db = get_db()

    # Look up the entry (must exist and not be deleted)
    entry = db.execute('''SELECT * FROM daily_entries
                          WHERE id = ? AND is_deleted = 0''',
                       (entry_id,)).fetchone()

    if entry is None:
        flash('Entry not found.', 'error')
        return redirect(url_for('daily'))

    if request.method == 'GET':
        return render_template('edit_entry.html', entry=entry)

    # POST — save the edits
    entry_date = request.form.get('date', '').strip()
    description = request.form.get('description', '').strip()
    amount_str = request.form.get('amount', '').strip()
    notes = request.form.get('notes', '').strip()

    # --- Validation (same rules as add) ---

    # Date must be valid
    try:
        parsed_date = date.fromisoformat(entry_date)
    except ValueError:
        flash('Invalid date format.', 'error')
        return render_template('edit_entry.html', entry=entry)

    # Date must not be in the future
    if parsed_date > date.today():
        flash('Future dates are not allowed.', 'error')
        return render_template('edit_entry.html', entry=entry)

    # Description is required
    if not description:
        if entry['type'] == 'recovery':
            flash('Salesman name is required.', 'error')
        else:
            flash('Description is required.', 'error')
        return render_template('edit_entry.html', entry=entry)

    # Amount must be a positive whole number
    try:
        amount = int(amount_str)
    except (ValueError, TypeError):
        flash('Amount must be a valid number.', 'error')
        return render_template('edit_entry.html', entry=entry)

    if amount <= 0:
        flash('Amount must be greater than 0.', 'error')
        return render_template('edit_entry.html', entry=entry)

    # --- Update the entry ---
    db.execute('''UPDATE daily_entries
                  SET date = ?, description = ?, amount = ?, notes = ?
                  WHERE id = ?''',
               (entry_date, description, amount, notes, entry_id))
    db.commit()

    flash('Entry updated.', 'success')
    return redirect(url_for('daily', date=entry_date))


