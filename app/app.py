def get_setting(db, key):
    """Get a single value from the settings table."""
    row = db.execute('SELECT value FROM settings WHERE key = ?',
                     (key,)).fetchone()
    return row['value'] if row else None


def get_opening_balance(db, current_date):
    """Calculate the opening balance for a given date.
    Walk back to find the cumulative balance from all prior entries.
    If no prior entries exist, use the opening_balance from settings.
    """
    initial_balance = int(get_setting(db, 'opening_balance') or 0)

    # Sum all recovery and expense entries before this date (non-deleted)
    row = db.execute('''
        SELECT
            COALESCE(SUM(CASE WHEN type = 'recovery' THEN amount ELSE 0 END), 0)
                AS total_recovery,
            COALESCE(SUM(CASE WHEN type = 'expense' THEN amount ELSE 0 END), 0)
                AS total_expenses
        FROM daily_entries
        WHERE date < ? AND is_deleted = 0
    ''', (current_date,)).fetchone()

    return initial_balance + row['total_recovery'] - row['total_expenses']


@app.route('/daily')
@login_required
def daily():
    """Show the daily register page for a given date, with optional monthly filter."""
    db = get_db()

    # --- Search & filter params ---
    search = request.args.get('search', '').strip()
    filter_payment = request.args.get('payment_type', 'all').strip()
    if filter_payment not in ('cash', 'online', 'all'):
        filter_payment = 'all'
    category = request.args.get('category', 'all').strip()
    valid_categories = ('all', 'food', 'transport', 'bank', 'utility', 'zakat', 'other')
    if category not in valid_categories:
        category = 'all'

    # --- Monthly filter ---
    # If month/year provided, show all entries for that month
    month_param = request.args.get('month')
    year_param = request.args.get('year')

    today = date.today()

    if month_param and year_param:
        try:
            selected_month = int(month_param)
            selected_year = int(year_param)
            if not (1 <= selected_month <= 12):
                raise ValueError
            if not (2000 <= selected_year <= 2100):
                raise ValueError
        except (ValueError, TypeError):
            selected_month = today.month
            selected_year = today.year
    else:
        selected_month = None
        selected_year = None

    # Get business name from settings
    business_name = get_setting(db, 'business_name') or 'Waqar & Brothers'

    if selected_month is not None:
        # --- MONTHLY VIEW ---
        month_str = str(selected_month).zfill(2)
        year_str = str(selected_year)

        # First day of selected month — used for opening balance
        first_of_month = f'{year_str}-{month_str}-01'
        opening_balance = get_opening_balance(db, first_of_month)

        # Get all recovery entries for the month (non-deleted)
        recovery_sql = '''
            SELECT id, date, description, amount, notes
            FROM daily_entries
            WHERE strftime('%m', date) = ? AND strftime('%Y', date) = ?
                  AND type = 'recovery' AND is_deleted = 0'''
        recovery_params = [month_str, year_str]
        if search:
            recovery_sql += ' AND description LIKE ?'
            recovery_params.append(f'%{search}%')
        recovery_sql += ' ORDER BY date ASC, id ASC'
        recoveries = db.execute(recovery_sql, recovery_params).fetchall()

        # Get all expense entries for the month (non-deleted)
        expense_sql = '''
            SELECT id, date, description, amount, notes, receipt, category
            FROM daily_entries
            WHERE strftime('%m', date) = ? AND strftime('%Y', date) = ?
                  AND type = 'expense' AND is_deleted = 0'''
        expense_params = [month_str, year_str]
        if search:
            expense_sql += ' AND description LIKE ?'
            expense_params.append(f'%{search}%')
        if filter_payment != 'all':
            expense_sql += ' AND payment_type = ?'
            expense_params.append(filter_payment)
        if category != 'all':
            expense_sql += ' AND category = ?'
            expense_params.append(category)
        expense_sql += ' ORDER BY date ASC, id ASC'
        expenses = db.execute(expense_sql, expense_params).fetchall()

        # Calculate totals
        total_recovery = sum(r['amount'] for r in recoveries)
        total_expenses = sum(e['amount'] for e in expenses)
        closing_balance = opening_balance + total_recovery - total_expenses

        # Display label for the month
        month_name = date(selected_year, selected_month, 1).strftime('%B %Y')

        # Previous and next month for navigation
        if selected_month == 1:
            prev_month, prev_year = 12, selected_year - 1
        else:
            prev_month, prev_year = selected_month - 1, selected_year

