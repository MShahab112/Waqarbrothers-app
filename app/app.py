# Lightweight in-memory cache for /reports (TTL = 8 seconds).
# Avoids redundant DB aggregation on rapid refreshes.
_reports_cache = {}
_REPORTS_CACHE_TTL = 8       # seconds
_CHART_DAY_LIMIT = 30        # max data points shown in charts


def _preload_dashboard_cache(db):
    """Warm the three report ranges /dashboard renders so the first visit
    after login is served entirely from cache. Best-effort — any failure
    here is swallowed so it can never block the login redirect.
    """
    try:
        business_name = get_setting(db, 'business_name') or 'Waqar & Brothers'
        today = date.today()
        today_iso = today.isoformat()
        month_start_iso = today.replace(day=1).isoformat()
        seven_start_iso = (today - timedelta(days=6)).isoformat()
        for d_from in (today_iso, month_start_iso, seven_start_iso):
            _compute_reports_data(db, business_name, d_from, today_iso)
    except Exception:
        pass


def _compute_reports_data(db, business_name, date_from, date_to,
                          filter_val='', type_filter='all'):
    """Build the reports context dict for a given date range.

    Results are cached in-memory for _REPORTS_CACHE_TTL seconds, keyed on
    (business_name, date_from, date_to, filter_val, type_filter). Mutations
    invalidate the cache via `_reports_cache.clear()` in
    add/edit/delete/settings routes.
    """
    cache_key = (business_name, date_from or '', date_to or '',
                 filter_val or '', type_filter or 'all')
    now = time.time()
    hit = _reports_cache.get(cache_key)
    if hit and hit[0] > now:
        # Shallow copy so route-specific additions don't pollute the cache
        return dict(hit[1])

    ctx = _compute_reports_data_uncached(db, business_name, date_from, date_to,
                                         filter_val, type_filter)

    # Evict expired entries + hard cap to keep memory bounded
    for k in [k for k, (exp, _) in _reports_cache.items() if exp <= now]:
        _reports_cache.pop(k, None)
    if len(_reports_cache) > 20:
        _reports_cache.clear()

    _reports_cache[cache_key] = (now + _REPORTS_CACHE_TTL, ctx)
    return dict(ctx)


def _compute_reports_data_uncached(db, business_name, date_from, date_to,
                                    filter_val='', type_filter='all'):
    """Actual DB-backed computation. Callers should prefer the cached wrapper."""
    cat_filter = ''
    desc_filter = ''
    if filter_val and '|' in filter_val:
        cat_filter, desc_filter = filter_val.split('|', 1)

    # Type filter pill — 'all' / 'recovery' / 'expense' / 'transfer' / 'drawing'.
    # Applied AFTER the existing cat/desc filter so users can combine a Name/
    # Purpose search with a type scope.
    type_filter = (type_filter or 'all').lower()
    if type_filter not in VALID_TYPE_FILTERS:
        type_filter = 'all'

    # Build the SQL fragment + params for the chosen pill. Returns (sql, params)
    # so we can append the same predicate to every aggregate query below.
    def _type_filter_clause():
        if type_filter == 'recovery':
            return (" AND type = 'recovery'", [])
        if type_filter == 'online':
            return (" AND type = 'online'", [])
        if type_filter == 'expense':
            return (
                " AND type = 'expense'"
                " AND is_bank_transfer = 0"
                " AND NOT " + DRAWING_PREDICATE_SQL,
                [],
            )
        if type_filter == 'transfer':
            return (" AND type = 'expense' AND is_bank_transfer = 1", [])
        if type_filter == 'drawing':
            return (
                " AND type = 'expense'"
                " AND is_bank_transfer = 0"
                " AND " + DRAWING_PREDICATE_SQL,
                [],
            )
        return ('', [])

    # For queries that read daily_entries directly (no is_bank_transfer column)
    # we need to inline the bank-transfer predicate via BANK_TRANSFER_PREDICATE_SQL.
    def _type_filter_clause_raw():
        if type_filter == 'recovery':
            return (" AND type = 'recovery'", [])
        if type_filter == 'online':
            return (" AND type = 'online'", [])
        if type_filter == 'expense':
            return (
                " AND type = 'expense'"
                " AND NOT " + BANK_TRANSFER_PREDICATE_SQL +
                " AND NOT " + DRAWING_PREDICATE_SQL,
                [],
            )
        if type_filter == 'transfer':
            return (
                " AND type = 'expense' AND " + BANK_TRANSFER_PREDICATE_SQL,
                [],
            )
        if type_filter == 'drawing':
            return (
                " AND type = 'expense'"
                " AND NOT " + BANK_TRANSFER_PREDICATE_SQL +
                " AND " + DRAWING_PREDICATE_SQL,
                [],
            )
        return ('', [])

    # Opening balance at the start of the filter range
    if date_from:
        period_opening = get_opening_balance(db, date_from)
    else:
        period_opening = int(get_setting(db, 'opening_balance') or 0)

    # Single daily-breakdown query. Overall totals are derived from these
    # rows in Python to save a second table scan.
    # Bank Slip exclusion reads is_bank_transfer from daily_entries_enriched
    # (business rule 4). The view is the single source of truth for the rule.
    daily_sql = '''
        SELECT
            date,
            COALESCE(SUM(CASE WHEN type = 'recovery' THEN amount ELSE 0 END), 0)
                AS recovery,
            COALESCE(SUM(CASE WHEN type = 'online' THEN amount ELSE 0 END), 0)
                AS online,
            COALESCE(SUM(CASE WHEN type = 'expense' AND is_bank_transfer = 0
                               THEN amount ELSE 0 END), 0)
                AS expense,
            COALESCE(SUM(CASE WHEN type = 'expense' AND is_bank_transfer = 1
                               THEN amount ELSE 0 END), 0)
                AS bank_transfer,
            COALESCE(SUM(CASE WHEN type = 'expense' AND is_bank_transfer = 0
                                AND ''' + DRAWING_PREDICATE_SQL + '''
                               THEN amount ELSE 0 END), 0)
                AS drawings,
            COALESCE(SUM(CASE WHEN type = 'expense' AND is_bank_transfer = 0
                                AND NOT ''' + DRAWING_PREDICATE_SQL + '''
                               THEN amount ELSE 0 END), 0)
                AS business_expense
        FROM daily_entries_enriched
        WHERE is_deleted = 0
    '''
    daily_params = []
    if date_from:
        daily_sql += ' AND date >= ?'
        daily_params.append(date_from)
    if date_to:
        daily_sql += ' AND date <= ?'
        daily_params.append(date_to)
    # NOTE: `cat_filter` / `desc_filter` intentionally do NOT narrow daily_sql.
    # The category dropdown is meant to zoom into a specific Expense Breakdown
    # slice (e.g. "show only Bank Slip rows in the breakdown list"). It must
    # not zero out the day-level Recovery / Online / Expense / Bank columns or
    # the Overview cards — those always reflect the full business activity for
    # the selected date range. The category filter is applied below to
    # `breakdown_sql` only, which is what feeds `category_totals`.
    # Step 1 — run the UNFILTERED aggregate first so the running balance and
    # closing balance always reflect true cash flow across ALL types, even
    # when a type_filter narrows the visible rows. Without this, applying a
    # filter like type_filter='online' zeroes out recovery/expense/bank rows
    # and the balance becomes `opening − online`, which is nonsense.
    full_sql = daily_sql + ' GROUP BY date ORDER BY date ASC'
    full_rows = execute_timed(db, full_sql, daily_params).fetchall()
    full_running = period_opening
    full_closing = period_opening
    balance_by_date = {}
    # True unfiltered totals — used for the business-wide "Cash vs Online"
    # ratio so the card stays meaningful even when a type filter is active.
    full_total_recovery = 0
    full_total_online = 0
    for row in full_rows:
        full_total_recovery += row['recovery']
        full_total_online += row['online']
        full_cash = row['recovery'] - row['online']
        full_running += full_cash - row['expense'] - row['bank_transfer']
        balance_by_date[row['date']] = full_running
        full_closing = full_running
    full_total_cash_recovery = full_total_recovery - full_total_online

    # Step 2 — run the optionally type-filtered aggregate for displayed totals.
    _tfc_sql, _tfc_params = _type_filter_clause()
    if _tfc_sql:
        filtered_sql = daily_sql + _tfc_sql + ' GROUP BY date ORDER BY date ASC'
        filtered_params = daily_params + list(_tfc_params)
        rows = execute_timed(db, filtered_sql, filtered_params).fetchall()
    else:
        rows = full_rows

    daily_data_asc = []
    total_recovery = 0
    total_online = 0
    total_cash_recovery = 0
    total_expense = 0
    total_bank_transfer = 0
    total_drawings = 0
    total_business_expense = 0
    for row in rows:
        rec = row['recovery']
        onl = row['online']
        exp = row['expense']
        bt = row['bank_transfer']
        dr = row['drawings']
        be = row['business_expense']
        total_recovery += rec
        total_online += onl
        cash = rec - onl
        total_cash_recovery += cash
        total_expense += exp
        total_bank_transfer += bt
        total_drawings += dr
        total_business_expense += be
        daily_data_asc.append({
            'date': row['date'],
            'recovery': rec,
            'recovery_cash': cash,
            'online': onl,
            'cash_recovery': cash,
            'expense': exp,
            'bank_transfer': bt,
            'drawings': dr,
            'business_expense': be,
            # Balance column always shows the TRUE running balance as of the
            # date, regardless of any type filter. Falls back to period_opening
            # if the date somehow isn't in the unfiltered set.
            'balance': balance_by_date.get(row['date'], period_opening),
        })
    closing_balance = full_closing
    # Business rule 6: Profit/Loss = Total Recovery − Business Expenses ONLY.
    # Explicitly excludes drawings (owner withdrawals) and bank transfers
    # (cash-to-bank, not outflow from business perspective).
    profit_loss = total_recovery - total_business_expense
    # Total Outflow = every PKR that left the cash drawer (expenses + transfers).
    # Useful for cash-position planning; NOT the same as the P/L "cost" side.
    total_outflow = total_expense + total_bank_transfer

    # Display table is DESC (newest first)
    daily_data = list(reversed(daily_data_asc))

    # Cash-based insights — best recovery day, highest expense day, highest balance day
    insights = None
    if daily_data_asc:
        best_rec = None
        high_exp = None
        high_bal = None
        for d in daily_data_asc:
            if d['recovery'] > 0 and (best_rec is None or d['recovery'] > best_rec['recovery']):
                best_rec = d
            if d['expense'] > 0 and (high_exp is None or d['expense'] > high_exp['expense']):
                high_exp = d
            if high_bal is None or d['balance'] > high_bal['balance']:
                high_bal = d
        insights = {
            'best_recovery_date': best_rec['date'] if best_rec else None,
            'best_recovery_amount': best_rec['recovery'] if best_rec else 0,
            'highest_expense_date': high_exp['date'] if high_exp else None,
            'highest_expense_amount': high_exp['expense'] if high_exp else 0,
            'highest_balance_date': high_bal['date'] if high_bal else None,
            'highest_balance_amount': high_bal['balance'] if high_bal else 0,
            'num_days': len(daily_data_asc),
        }

    # Chart data — chronological order (ASC), capped to last N days.
    # chart_expense is kept for back-compat; chart_business_expense +
    # chart_drawings drive the updated 3-series bar chart.
    chart_dates = [d['date'] for d in daily_data_asc]
    chart_recovery = [d['recovery'] for d in daily_data_asc]
    chart_expense = [d['expense'] for d in daily_data_asc]
    chart_business_expense = [d['business_expense'] for d in daily_data_asc]
    chart_bank_transfer = [d['bank_transfer'] for d in daily_data_asc]
    chart_drawings = [d['drawings'] for d in daily_data_asc]
    chart_balance = [d['balance'] for d in daily_data_asc]

    chart_dates = chart_dates[-_CHART_DAY_LIMIT:]
    chart_recovery = chart_recovery[-_CHART_DAY_LIMIT:]
    chart_expense = chart_expense[-_CHART_DAY_LIMIT:]
    chart_business_expense = chart_business_expense[-_CHART_DAY_LIMIT:]
    chart_bank_transfer = chart_bank_transfer[-_CHART_DAY_LIMIT:]
    chart_drawings = chart_drawings[-_CHART_DAY_LIMIT:]
    chart_balance = chart_balance[-_CHART_DAY_LIMIT:]

    # --- Expense breakdown by (category, payment_type) in ONE pass ---
    # The UI needs two separate aggregates (category totals, payment totals).
    # We fold them from a single grouped scan to avoid two table hits.
    # Business rule 4: a row is a Bank Slip transfer (NOT a business
    # expense) when category='bank_slip' OR payment_type='bank_slip'. Such
    # rows still appear in payment_breakdown so the Bank vs Cash card
    # totals match the underlying ledger.
    breakdown_sql = '''
        SELECT category                          AS real_cat,
               TRIM(description)                 AS desc,
               COALESCE(payment_type, 'cash')    AS pt,
               COALESCE(SUM(amount), 0)          AS total
        FROM daily_entries
        WHERE is_deleted = 0 AND type = 'expense'
    '''
    breakdown_params = []
    if date_from:
        breakdown_sql += ' AND date >= ?'
        breakdown_params.append(date_from)
    if date_to:
        breakdown_sql += ' AND date <= ?'
        breakdown_params.append(date_to)
    if desc_filter:
        breakdown_sql += ' AND TRIM(description) COLLATE NOCASE = ?'
        breakdown_params.append(desc_filter)
    elif cat_filter:
        # The category dropdown maps to *logical* buckets, not raw
        # daily_entries.category values. Drawings, for example, are stored as
        # category='expense' with a description matching waqar/waqas/home; a
        # naive "AND category = 'drawing'" would return nothing. These
        # predicates match exactly how the rest of the app classifies rows
        # (see BANK_TRANSFER_PREDICATE_SQL and DRAWING_PREDICATE_SQL).
        if cat_filter == 'bank_slip':
            breakdown_sql += ' AND ' + BANK_TRANSFER_PREDICATE_SQL
        elif cat_filter == 'drawing':
            breakdown_sql += (' AND NOT ' + BANK_TRANSFER_PREDICATE_SQL +
                              ' AND ' + DRAWING_PREDICATE_SQL)
        elif cat_filter == 'advance':
            breakdown_sql += (' AND NOT ' + BANK_TRANSFER_PREDICATE_SQL +
                              " AND COALESCE(category, 'other') = 'advance'")
        elif cat_filter == 'expense':
            breakdown_sql += (' AND NOT ' + BANK_TRANSFER_PREDICATE_SQL +
                              ' AND NOT ' + DRAWING_PREDICATE_SQL +
                              " AND COALESCE(category, 'other') = 'expense'")
        elif cat_filter == 'other':
            breakdown_sql += (' AND NOT ' + BANK_TRANSFER_PREDICATE_SQL +
                              ' AND NOT ' + DRAWING_PREDICATE_SQL +
                              " AND COALESCE(category, 'other') = 'other'")
        else:
            breakdown_sql += ' AND category = ?'
            breakdown_params.append(cat_filter)
    # Type-filter scopes which expense rows feed the Purpose breakdown.
    # breakdown_sql already hard-codes type='expense', so a recovery pill
    # yields an empty aggregate here — intentional.
    if type_filter == 'recovery':
        breakdown_sql += ' AND 1 = 0'
    elif type_filter == 'expense':
        breakdown_sql += (' AND NOT ' + BANK_TRANSFER_PREDICATE_SQL +
                          ' AND NOT ' + DRAWING_PREDICATE_SQL)
    elif type_filter == 'transfer':
        breakdown_sql += ' AND ' + BANK_TRANSFER_PREDICATE_SQL
    elif type_filter == 'drawing':
        breakdown_sql += (' AND NOT ' + BANK_TRANSFER_PREDICATE_SQL +
                          ' AND ' + DRAWING_PREDICATE_SQL)
    breakdown_sql += ' GROUP BY real_cat, desc, pt'

    # Key the breakdown by (category, description) so each purpose shows on
    # its own row. Advances, drawings, bank slips, and regular expenses all
    # expand to per-description detail — e.g. "Waqar / Waqas / Home" instead
    # of one collapsed "Drawing" line, "Ali Advance / Bilal Advance" instead
    # of a single "Advance" line. When the user filters by a category, this
    # is how they see meaningful detail in the breakdown.
    category_breakdown = {}
    payment_breakdown = {'cash': 0, 'bank_slip': 0}
    # Bank Slip rollup — per business rule 4, these are cash-to-bank transfers,
    # NOT business expenses, so they must NOT contribute to total_expense or
    # profit/loss. We still collect the aggregate separately so the Expense
    # Breakdown list can surface it as an informational row alongside real
    # expense categories (user request: "show bank slip in expense breakdown").
    # When the user explicitly filters by cat_filter='bank_slip' we instead
    # expand bank slips into per-description rows so they can see the detail.
    bank_slip_total = 0
    expand_bank_slips = (cat_filter == 'bank_slip')
    for r in execute_timed(db, breakdown_sql, breakdown_params).fetchall():
        pay_key = r['pt'] if r['pt'] in payment_breakdown else 'cash'
        payment_breakdown[pay_key] += r['total']
        is_bank_transfer = is_bank_transfer_row(r['real_cat'], pay_key)
        if is_bank_transfer and not expand_bank_slips and type_filter != 'transfer':
            # Keep out of category_breakdown (which feeds business-expense
            # categories) but accumulate into the dedicated bank-slip bucket
            # for the single roll-up row.
            bank_slip_total += r['total']
            continue
        key = (r['real_cat'] or 'other', (r['desc'] or 'Other').title())
        category_breakdown[key] = category_breakdown.get(key, 0) + r['total']

    category_totals = []
    for (cat_key, name_key), v in category_breakdown.items():
        if v <= 0:
            continue
        # Tag rows visually as transfers when the underlying category is a
        # bank slip, so the template can keep them obviously distinct (blue
        # bar + "Transfer" pill) even when the user filtered in to see them.
        entry = {
            'key': name_key,
            'name': name_key,
            'amount': v,
            'category': cat_key,
        }
        if cat_key == 'bank_slip':
            entry['is_transfer'] = True
        category_totals.append(entry)
    # Append the aggregated Bank Slip row last (only when we didn't already
    # expand bank slips into per-description rows). Flagged with
    # `is_transfer=True` so the template can tag it visually and make it clear
    # this amount is NOT included in the Expense Breakdown header total.
    if bank_slip_total > 0:
        category_totals.append({
            'key': 'bank_slip',
            'name': CATEGORY_LABELS['bank_slip'],
            'amount': bank_slip_total,
            'category': 'bank_slip',
            'is_transfer': True,
        })
    category_totals.sort(key=lambda x: x['amount'], reverse=True)

    # --- Advance breakdown (per-person totals) ---
    # For the 'Advance' category, description stores the person's name.
    # Group case-insensitively on trimmed description, render with the first
    # casing we saw. Only runs when the selected filter includes Advance.
    advance_breakdown = []
    # If there is a desc filter, we only show advance breakdown if the description is an advance person.
    # We can just run it with the desc_filter on description.
    adv_sql = f'''
        SELECT MIN(TRIM(description))    AS name,
               COALESCE(SUM(amount), 0)  AS total,
               COUNT(*)                  AS entries
        FROM daily_entries
        WHERE is_deleted = 0
          AND type = 'expense'
          AND category = 'advance'
          AND TRIM(COALESCE(description, '')) != ''
    '''
    adv_params = []
    if date_from:
        adv_sql += ' AND date >= ?'
        adv_params.append(date_from)
    if date_to:
        adv_sql += ' AND date <= ?'
        adv_params.append(date_to)
    if desc_filter:
        adv_sql += ' AND TRIM(description) COLLATE NOCASE = ?'
        adv_params.append(desc_filter)
    # Advance rows are category='advance' — not recovery, not transfers, not
    # drawings. Suppress the advance breakdown under any non-all/expense pill.
    if type_filter in ('recovery', 'transfer', 'drawing'):
        adv_sql += ' AND 1 = 0'
    adv_sql += f' GROUP BY {CANONICAL_KEY_SQL} ORDER BY total DESC'
    advance_breakdown = [
        {'name': r['name'], 'amount': r['total'], 'entries': r['entries']}
        for r in execute_timed(db, adv_sql, adv_params).fetchall()
        if r['total'] > 0
    ]

    # --- Salesman performance (grouped by salesman_id via JOIN) ---
    # Modern rows carry salesman_id. Legacy recovery rows that predate the
    # migration may still have NULL salesman_id — those are grouped under
    # their trimmed description so historical reports stay complete.
    sm_sql = '''
        SELECT s.id            AS sid,
               s.name          AS name,
               s.is_active     AS is_active,
               COALESCE(SUM(e.amount), 0) AS total,
               COUNT(e.id)                AS entries
        FROM salesmen s
        JOIN daily_entries e
          ON e.salesman_id = s.id
         AND e.type = 'recovery'
         AND e.is_deleted = 0
        WHERE s.is_deleted = 0
    '''
    sm_params = []
    if date_from:
        sm_sql += ' AND e.date >= ?'
        sm_params.append(date_from)
    if date_to:
        sm_sql += ' AND e.date <= ?'
        sm_params.append(date_to)
    # Salesman performance only makes sense when recovery rows are in scope.
    # Non-recovery pills should show an empty salesman table.
    if type_filter in ('expense', 'online', 'transfer', 'drawing'):
        sm_sql += ' AND 1 = 0'
    sm_sql += ' GROUP BY s.id, s.name, s.is_active ORDER BY total DESC'

    sm_rows = execute_timed(db, sm_sql, sm_params).fetchall()
    salesmen = [
        {
            'id': r['sid'],
            'name': r['name'],
            'is_active': bool(r['is_active']),
            'total': r['total'],
            'entries': r['entries'],
        }
        for r in sm_rows if r['total'] > 0
    ]

    # Legacy fallback: pre-migration recovery rows with NULL salesman_id
    legacy_sql = '''
        SELECT TRIM(description) AS name,
               COALESCE(SUM(amount), 0) AS total,
               COUNT(*) AS entries
        FROM daily_entries
        WHERE is_deleted = 0 AND type = 'recovery'
          AND salesman_id IS NULL
          AND TRIM(COALESCE(description, '')) != ''
    '''
    legacy_params = []
    if date_from:
        legacy_sql += ' AND date >= ?'
        legacy_params.append(date_from)
    if date_to:
        legacy_sql += ' AND date <= ?'
        legacy_params.append(date_to)
    if type_filter in ('expense', 'online', 'transfer', 'drawing'):
        legacy_sql += ' AND 1 = 0'
    legacy_sql += ' GROUP BY TRIM(LOWER(description))'

    legacy_rows = execute_timed(db, legacy_sql, legacy_params).fetchall()
    # Merge legacy rows into the list by name (case-insensitive)
    by_name = {s['name'].lower(): s for s in salesmen}
    for r in legacy_rows:
        key = (r['name'] or '').strip().lower()
        if not key:
            continue
        if key in by_name:
            by_name[key]['total'] += r['total']
            by_name[key]['entries'] += r['entries']
        else:
            salesmen.append({
                'id': None,
                'name': r['name'].strip(),
                'is_active': True,
                'total': r['total'],
                'entries': r['entries'],
            })
    salesmen.sort(key=lambda s: s['total'], reverse=True)
    top_salesmen = salesmen[:3]

    # Chart data for salesman bar chart (cap at top 10 for readability)
    chart_salesmen_labels = [s['name'] for s in salesmen[:10]]
    chart_salesmen_values = [s['total'] for s in salesmen[:10]]

    # --- Dynamic Descriptions for Filter ---
    descriptions_raw = execute_timed(db, '''
        SELECT DISTINCT category, TRIM(description) AS desc
        FROM daily_entries
        WHERE type='expense' AND is_deleted=0 AND TRIM(COALESCE(description, '')) != ''
        ORDER BY desc ASC
    ''').fetchall()
    dynamic_descriptions = []
    seen = set()
    for r in descriptions_raw:
        cat = r['category']
        desc = r['desc'].title()
        key = f"{cat}|{desc}"
        if key not in seen:
            seen.add(key)
            cat_label = CATEGORY_LABELS.get(cat, cat.title() if cat else 'Other')
            dynamic_descriptions.append({
                'value': key,
                'label': f"{desc} ({cat_label})"
            })

    online_source_breakdown = {k: 0 for k in ONLINE_SOURCE_VALUES}
    online_sql = '''
        SELECT COALESCE(online_source, 'other') AS source, COALESCE(SUM(amount), 0) AS total
        FROM daily_entries
        WHERE is_deleted = 0 AND type = 'online'
    '''
    online_params = []
    if date_from:
        online_sql += ' AND date >= ?'
        online_params.append(date_from)
    if date_to:
        online_sql += ' AND date <= ?'
        online_params.append(date_to)
    online_sql += ' GROUP BY COALESCE(online_source, \'other\')'
    for row in execute_timed(db, online_sql, online_params).fetchall():
        src = row['source'] if row['source'] in online_source_breakdown else 'other'
        online_source_breakdown[src] += row['total']
    total_online = sum(online_source_breakdown.values())
    # "Cash vs Online Ratio" is a business-wide collection-mix metric and must
    # NOT be skewed by a type filter. Always compute it from the true
    # unfiltered totals (captured from full_rows above), independent of
    # type_filter / online_source narrowing applied elsewhere.
    total_cash_recovery = full_total_cash_recovery
    cash_online_base = full_total_recovery
    if cash_online_base > 0:
        cash_ratio_pct = round((full_total_cash_recovery * 100.0) / cash_online_base, 1)
        online_ratio_pct = round((full_total_online * 100.0) / cash_online_base, 1)
    else:
        cash_ratio_pct = 0.0
        online_ratio_pct = 0.0

    # --- Build context dict ---
    return dict(business_name=business_name,
                period_opening=period_opening,
                total_recovery=total_recovery,
                total_expense=total_expense,
                total_bank_transfer=total_bank_transfer,
                total_drawings=total_drawings,
                total_business_expense=total_business_expense,
                total_outflow=total_outflow,
                profit_loss=profit_loss,
                closing_balance=closing_balance,
                daily_data=daily_data,
                date_from=date_from,
                date_to=date_to,
                insights=insights,
                chart_dates=chart_dates,
                chart_recovery=chart_recovery,
                chart_expense=chart_expense,
                chart_business_expense=chart_business_expense,
                chart_bank_transfer=chart_bank_transfer,
                chart_drawings=chart_drawings,
                chart_balance=chart_balance,
                category_totals=category_totals,
                advance_breakdown=advance_breakdown,
                filter_val=filter_val or '',
                type_filter=type_filter,
                dynamic_descriptions=dynamic_descriptions,
                category_labels=CATEGORY_LABELS,
                valid_categories=VALID_CATEGORIES,
                payment_breakdown=payment_breakdown,
                total_online=total_online,
                total_cash_recovery=total_cash_recovery,
                cash_ratio_pct=cash_ratio_pct,
                online_ratio_pct=online_ratio_pct,
                online_source_labels=ONLINE_SOURCE_LABELS,
                online_source_breakdown=online_source_breakdown,
                salesmen=salesmen,
                top_salesmen=top_salesmen,
                chart_salesmen_labels=chart_salesmen_labels,
                chart_salesmen_values=chart_salesmen_values)


