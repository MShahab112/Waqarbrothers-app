def _set_cloud_backup_status(db, ok, detail, path=None):
    """Record the outcome of the most recent cloud copy so the Settings page
    can show the user whether Google Drive / OneDrive received the file."""
    try:
        now_str = datetime.now().strftime('%Y-%m-%d %I:%M %p').lstrip('0')
        db.execute('INSERT OR REPLACE INTO settings (key, value) VALUES (?, ?)',
                   ('last_cloud_backup_time', now_str))
        db.execute('INSERT OR REPLACE INTO settings (key, value) VALUES (?, ?)',
                   ('last_cloud_backup_status',
                    'success' if ok else f'error: {detail}'))
        if path:
            db.execute('INSERT OR REPLACE INTO settings (key, value) VALUES (?, ?)',
                       ('last_cloud_backup_path', path))
        db.commit()
    except Exception:
        # Status tracking must never take down the backup flow itself.
        pass


def copy_to_cloud_backup(db, src_path, rel_dest, silent=False):
    """Copy `src_path` to `<cloud_folder>/<rel_dest>`.

    Returns (ok: bool, detail: str). NEVER raises. If the cloud folder isn't
    configured, returns (False, 'not configured') without writing anything.
    All failures are logged to logs/cloud_backup.log.

    When `silent=False` (default), success/failure is also recorded in the
    settings table so the Settings page shows "last cloud copy" status. Used
    for DB backups + day-close artifacts (user-facing events).

    When `silent=True`, the settings table is NOT updated — used for routine
    per-receipt mirroring so the status display keeps tracking meaningful
    (DB-level) events rather than flipping on every image upload.
    """
    folder = _get_cloud_backup_folder(db)
    if not folder:
        return False, 'not configured'

    try:
        if not os.path.isdir(folder):
            raise FileNotFoundError(
                f'Cloud folder does not exist: {folder}')

        dest_path = os.path.join(folder, rel_dest)
        dest_dir = os.path.dirname(dest_path)
        os.makedirs(dest_dir, exist_ok=True)

        shutil.copy2(src_path, dest_path)
        if not silent:
            _set_cloud_backup_status(db, True, '', dest_path)
        return True, dest_path
    except Exception as e:
        detail = f'{type(e).__name__}: {e}'
        try:
            log_path = os.path.join(LOGS_DIR, 'cloud_backup.log')
            with open(log_path, 'a', encoding='utf-8') as f:
                f.write(f'{datetime.now().isoformat()} — '
                        f'failed "{rel_dest}": {detail}\n')
        except Exception:
            pass
        if not silent:
            _set_cloud_backup_status(db, False, detail)
        return False, detail


def backup_current_db():
    """Create a timestamped safety copy of the current database.
    Returns the filename of the backup, e.g. accounts_old_2026-04-11_19-30.db"""
    timestamp = datetime.now().strftime('%Y-%m-%d_%H-%M')
    safety_name = f'accounts_old_{timestamp}.db'
    safety_path = os.path.join(os.path.dirname(DATABASE), safety_name)
    shutil.copy2(DATABASE, safety_path)
    return safety_name


