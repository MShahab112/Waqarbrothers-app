class _NumberedCanvas:
    """Canvas wrapper that renders 'Page X of Y' on every page."""

    @staticmethod
    def make(brand_hex='#5d6d7e'):
        from reportlab.pdfgen import canvas as pdf_canvas
        from reportlab.lib.units import mm
        from reportlab.lib import colors

        color = colors.HexColor(brand_hex)

        class Numbered(pdf_canvas.Canvas):
            def __init__(self, *args, **kwargs):
                pdf_canvas.Canvas.__init__(self, *args, **kwargs)
                self._saved_page_states = []

            def showPage(self):
                self._saved_page_states.append(dict(self.__dict__))
                self._startPage()

            def save(self):
                total = len(self._saved_page_states)
                for state in self._saved_page_states:
                    self.__dict__.update(state)
                    self._draw_watermark()
                    self._draw_page_number(total)
                    pdf_canvas.Canvas.showPage(self)
                pdf_canvas.Canvas.save(self)

            def _draw_watermark(self):
                self.saveState()
                self.setFont('Helvetica-Bold', 48)
                self.setFillColorRGB(0.92, 0.93, 0.95, alpha=0.35)
                w, h = self._pagesize
                self.translate(w / 2, h / 2)
                self.rotate(45)
                self.drawCentredString(0, 0, 'Waqar Brothers')
                self.restoreState()

            def _draw_page_number(self, total):
                self.setFont('Helvetica', 8)
                self.setFillColor(color)
                self.drawRightString(
                    self._pagesize[0] - 20*mm, 10*mm,
                    f'Page {self._pageNumber} of {total}')

        return Numbered


def _build_pdf_document(elements):
    """Build a ReportLab PDF in memory and return BytesIO at position 0."""
    from reportlab.lib.pagesizes import A4
    from reportlab.lib.units import mm
    from reportlab.platypus import SimpleDocTemplate

    output = io.BytesIO()
    doc = SimpleDocTemplate(output, pagesize=A4,
                             leftMargin=20*mm, rightMargin=20*mm,
                             topMargin=18*mm, bottomMargin=18*mm)
    doc.build(elements, canvasmaker=_NumberedCanvas.make())
    output.seek(0)
    return output


def _build_daily_pdf_bytes(db, date_str):
    """Build a Daily Closing Report PDF for the given date.
    Returns a BytesIO positioned at 0. Raises on failure.

    This is a small helper for Close Day — it is independent of the
    existing /export/pdf route so that route's logic stays untouched.
    """
    from reportlab.lib import colors
    from reportlab.platypus import Table, Paragraph, Spacer

    s = _pdf_styles()
    c = s['colors']

    report_date = date.fromisoformat(date_str)
    business_name = get_setting(db, 'business_name') or 'Waqar & Brothers'
    opening_balance = get_opening_balance(db, date_str)

    recoveries = db.execute('''
        SELECT description, amount
        FROM daily_entries
        WHERE date = ? AND type = 'recovery' AND is_deleted = 0
        ORDER BY id ASC
    ''', (date_str,)).fetchall()

    expenses = db.execute('''
        SELECT description, amount
        FROM daily_entries
        WHERE date = ? AND type = 'expense' AND is_deleted = 0
        ORDER BY id ASC
    ''', (date_str,)).fetchall()

    total_recovery = sum(r['amount'] for r in recoveries)
    total_expenses = sum(e['amount'] for e in expenses)
    closing_balance = opening_balance + total_recovery - total_expenses

    entry_ts = _pdf_entry_table_style(c)

    elements = []

    _pdf_header_block(elements, business_name,
                      f'Daily Closing Report &mdash; {report_date.strftime("%d %B %Y")}',
                      None)

    # Opening balance badge
    ob_data = [['Opening Balance', format_pkr(opening_balance)]]
    ob = Table(ob_data, colWidths=[330, 120])
    ob.setStyle(_pdf_totals_row_style(c['light_bg'], c['border']))
    elements.append(ob)
    elements.append(Spacer(1, 14))

    # Recovery section
    elements.append(Paragraph('Recovery Entries', s['section']))
    if recoveries:
        data = [['#', 'Salesman', 'Amount']]
        for i, r in enumerate(recoveries, 1):
            data.append([str(i), r['description'], format_pkr(r['amount'])])
        t = Table(data, colWidths=[30, 300, 120])
        t.setStyle(entry_ts)
        elements.append(t)
    else:
        elements.append(Paragraph('No recovery entries for this day.',
                                   s['base']['Normal']))
    elements.append(Spacer(1, 6))
    tr = Table([['Total Recovery', format_pkr(total_recovery)]],
               colWidths=[330, 120])
    tr.setStyle(_pdf_totals_row_style(c['green_bg'], c['green_accent']))
    elements.append(tr)
    elements.append(Spacer(1, 14))

    # Expense section
    elements.append(Paragraph('Expense Entries', s['section']))
    if expenses:
        data = [['#', 'Description', 'Amount']]
        for i, ex in enumerate(expenses, 1):
            data.append([str(i), ex['description'], format_pkr(ex['amount'])])
        t = Table(data, colWidths=[30, 300, 120])
        t.setStyle(entry_ts)
        elements.append(t)
    else:
        elements.append(Paragraph('No expense entries for this day.',
                                   s['base']['Normal']))
    elements.append(Spacer(1, 6))
    te = Table([['Total Expenses', format_pkr(total_expenses)]],
               colWidths=[330, 120])
    te.setStyle(_pdf_totals_row_style(c['red_bg'], c['red_accent']))
    elements.append(te)
    elements.append(Spacer(1, 18))

    # Summary
    elements.append(Paragraph('Summary', s['section']))
    summary_data = [
        ['Opening Balance', format_pkr(opening_balance)],
        ['+ Total Recovery', format_pkr(total_recovery)],
        ['- Total Expenses', format_pkr(total_expenses)],
        ['= Closing Balance', format_pkr(closing_balance)],
    ]
    elements.append(_pdf_summary_table(summary_data, c))

    closed_at = datetime.now().strftime('%d %B %Y, %I:%M %p')
    _pdf_footer_block(elements, extra_line=f'Day closed on {closed_at}')

    return _build_pdf_document(elements)


@app.route('/close-day', methods=['POST'])
@login_required
def close_day():
    db = get_db()
    date_str = request.form.get('date', '').strip()

    if not date_str:
        flash('Invalid date.', 'error')
        return redirect(url_for('daily'))

    # Validate date format
    try:
        date.fromisoformat(date_str)
    except ValueError:
        flash('Invalid date format.', 'error')
        return redirect(url_for('daily'))

    # Ensure date-specific backup folder exists
    day_folder = day_backup_folder(date_str)
    try:
        os.makedirs(day_folder, exist_ok=True)
    except Exception as e:
        flash(f'Could not create backup folder: {e}', 'error')
        return redirect(url_for('daily', date=date_str))

    # Check if already closed (but allow re-close if the day was reopened)
    closed_file = os.path.join(day_folder, f'accounts_{date_str}_closed.db')
    reopened_file = os.path.join(
        day_folder, f'accounts_{date_str}_reopened.txt')

    if os.path.exists(closed_file) and not os.path.exists(reopened_file):
        flash('Day already closed.', 'error')
        return redirect(url_for('daily', date=date_str))

    # If the day had been reopened, clear the reopen marker so the fresh
    # close takes effect. The existing closed.db is simply overwritten
    # below — we never touch historical backups besides this same file.
    if os.path.exists(reopened_file):
        try:
            os.remove(reopened_file)
        except Exception as e:
            flash(f'Could not clear reopen marker: {e}', 'error')
            return redirect(url_for('daily', date=date_str))

    # STEP 1 — create (or overwrite) the closed DB backup
    try:
        shutil.copy2(DATABASE, closed_file)
    except Exception as e:
        flash(f'Error closing day: {str(e)}', 'error')
        return redirect(url_for('daily', date=date_str))

    # STEP 2 — build PDF report (reuses shared helper)
    try:
        pdf_buffer = _build_daily_pdf_bytes(db, date_str)
    except Exception as e:
        flash(f'Day closed but PDF generation failed: {str(e)}', 'error')
        return redirect(url_for('daily', date=date_str))

    # STEP 3 — save PDF to date-specific backup folder
    pdf_filename = f'daily_report_{date_str}.pdf'
    pdf_path = os.path.join(day_folder, pdf_filename)
    try:
        with open(pdf_path, 'wb') as f:
            f.write(pdf_buffer.getvalue())
    except Exception as e:
        # Not fatal — the DB backup is the critical artifact; still download
        flash(f'PDF download ready, but saving to backups failed: {str(e)}',
              'error')

    # STEP 4 — send PDF as download
    pdf_buffer.seek(0)
    return send_file(pdf_buffer,
                     as_attachment=True,
                     download_name=pdf_filename,
                     mimetype='application/pdf')


@app.route('/reopen-day', methods=['POST'])
@login_required
def reopen_day():
    """Re-open a previously closed day so entries can be edited again.
    Does NOT delete or modify the existing closed backup file.
    Creates a marker file accounts_<date>_reopened.txt which is_day_closed()
    checks to decide the editable state."""
    db = get_db()
    date_str = request.form.get('date', '').strip()
    pin = request.form.get('pin', '').strip()

    if not date_str:
        flash('Invalid date.', 'error')
        return redirect(url_for('daily'))

    # Validate date format
    try:
        date.fromisoformat(date_str)
    except ValueError:
        flash('Invalid date format.', 'error')
        return redirect(url_for('daily'))

    # Require a valid 4-digit admin PIN before doing anything else
    if not verify_admin_pin(db, pin):
        flash('Invalid PIN.', 'error')
        return redirect(url_for('daily', date=date_str))

    # Ensure date-specific backup folder exists
    day_folder = day_backup_folder(date_str)
    try:
        os.makedirs(day_folder, exist_ok=True)
    except Exception as e:
        flash(f'Could not access backup folder: {e}', 'error')
        return redirect(url_for('daily', date=date_str))

    # Must have been closed before it can be reopened
    closed_file = os.path.join(
        day_folder, f'accounts_{date_str}_closed.db')
    if not os.path.exists(closed_file):
        flash('This day is not closed, so it cannot be reopened.', 'error')
        return redirect(url_for('daily', date=date_str))

    # Create reopened marker file (safe — never touches the closed.db backup)
    reopened_file = os.path.join(
        day_folder, f'accounts_{date_str}_reopened.txt')

    # Block duplicate reopens
    if os.path.exists(reopened_file):
        flash('Day already reopened.', 'error')
        return redirect(url_for('daily', date=date_str))

    try:
        timestamp = datetime.now().strftime('%Y-%m-%d %I:%M %p')
        user_label = session.get('username', 'unknown')
        with open(reopened_file, 'w', encoding='utf-8') as f:
            f.write(f'Day {date_str} reopened on {timestamp} by {user_label}\n')
    except Exception as e:
        flash(f'Error reopening day: {str(e)}', 'error')
        return redirect(url_for('daily', date=date_str))

    # Append to reopen log
    try:
        logs_folder = os.path.join(BASE_DIR, 'logs')
        os.makedirs(logs_folder, exist_ok=True)
        log_path = os.path.join(logs_folder, 'reopen.log')
        log_stamp = datetime.now().strftime('%Y-%m-%d %I:%M %p')
        with open(log_path, 'a', encoding='utf-8') as f:
            f.write(f'{log_stamp} — Day {date_str} reopened by '
                    f'{session.get("username", "unknown")}\n')
    except Exception:
        # Logging failure must not block the reopen action
        pass

    flash('Day reopened successfully.', 'success')
    return redirect(url_for('daily', date=date_str))


@app.route('/change-pin', methods=['POST'])
@login_required
def change_pin():
    """Change the admin PIN used to authorize Re-open Day.
    Requires the logged-in user's own login password as re-auth —
    this collapses the original two-step flow into one secure POST."""
    db = get_db()
    current_password = request.form.get('current_password', '')
    new_pin = request.form.get('new_pin', '').strip()
    confirm_pin = request.form.get('confirm_pin', '').strip()

    # Re-verify the logged-in user's password
    user = db.execute('SELECT password FROM users WHERE id = ?',
                      (session.get('user_id'),)).fetchone()
    if not user or not check_password_hash(user['password'], current_password):
        flash('Incorrect login password.', 'error')
        return redirect(url_for('settings'))

    # Validate the new PIN
    if not (new_pin.isdigit() and len(new_pin) == 4):
        flash('PIN must be exactly 4 digits.', 'error')
        return redirect(url_for('settings'))

    if new_pin != confirm_pin:
        flash('PINs do not match.', 'error')
        return redirect(url_for('settings'))

    set_admin_pin(db, new_pin)
    flash('PIN updated successfully.', 'success')
    return redirect(url_for('settings'))


