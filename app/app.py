@app.route('/dashboard')
@login_required
def dashboard():
    """Show dashboard with totals, monthly breakdown, and best/worst months."""
    db = get_db()

    business_name = get_setting(db, 'business_name') or 'Waqar & Brothers'

    # Overall totals (all non-deleted entries)
    totals = db.execute('''
        SELECT
            COALESCE(SUM(CASE WHEN type = 'recovery' THEN amount ELSE 0 END), 0)
                AS total_income,
            COALESCE(SUM(CASE WHEN type = 'expense' THEN amount ELSE 0 END), 0)
                AS total_expense
        FROM daily_entries
        WHERE is_deleted = 0
    ''').fetchone()

    total_income = totals['total_income']
    total_expense = totals['total_expense']
    net_profit = total_income - total_expense

    # Monthly breakdown — group by year-month
    rows = db.execute('''
        SELECT
            strftime('%Y', date) AS year,
            strftime('%m', date) AS month,
            COALESCE(SUM(CASE WHEN type = 'recovery' THEN amount ELSE 0 END), 0)
                AS income,
            COALESCE(SUM(CASE WHEN type = 'expense' THEN amount ELSE 0 END), 0)
                AS expense
        FROM daily_entries
        WHERE is_deleted = 0
        GROUP BY strftime('%Y', date), strftime('%m', date)
        ORDER BY year ASC, month ASC
    ''').fetchall()

    # Build monthly_data list with profit calculated
    month_names = ['', 'Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun',
                   'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec']

    monthly_data = []
    for row in rows:
        m = int(row['month'])
        y = int(row['year'])
        income = row['income']
        expense = row['expense']
        profit = income - expense
        monthly_data.append({
            'year': y,
            'month': m,
            'month_name': month_names[m],
            'label': f"{month_names[m]} {y}",
            'income': income,
            'expense': expense,
            'profit': profit,
        })

    # Best and worst months by profit
    best_month = None
    worst_month = None
    if monthly_data:
        best_month = max(monthly_data, key=lambda x: x['profit'])
        worst_month = min(monthly_data, key=lambda x: x['profit'])

    # Category-wise expense totals (skip categories with 0)
    cat_rows = db.execute('''
        SELECT COALESCE(category, 'other') AS cat, SUM(amount) AS total
        FROM daily_entries
        WHERE type = 'expense' AND is_deleted = 0
        GROUP BY cat
        ORDER BY total DESC
    ''').fetchall()

    category_labels = {
        'food': 'Food / Tea',
        'transport': 'Transport',
        'bank': 'Bank Slip',
        'utility': 'Utility Bill',
        'zakat': 'Zakat / Charity',
        'other': 'Other',
    }
    category_totals = []
    for row in cat_rows:
        if row['total'] > 0:
            category_totals.append({
                'name': category_labels.get(row['cat'], row['cat'].capitalize()),
                'amount': row['total'],
            })

    return render_template('dashboard.html',
                           business_name=business_name,
                           total_income=total_income,
                           total_expense=total_expense,
                           net_profit=net_profit,
                           monthly_data=monthly_data,
                           best_month=best_month,
                           worst_month=worst_month,
                           category_totals=category_totals)


