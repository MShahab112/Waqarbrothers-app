@app.route('/change-password', methods=['GET', 'POST'])
@login_required
def change_password():
    """Force password change on first login, or voluntary change."""
    db = get_db()

    # Check if this is a forced change (first login with default password)
    user = db.execute('SELECT must_change_password FROM users WHERE id = ?',
                      (session['user_id'],)).fetchone()
    forced = user['must_change_password'] == 1

    if request.method == 'GET':
        return render_template('change_password.html', forced=forced)

    # POST — process the password change
    new_password = request.form.get('new_password', '')
    confirm_password = request.form.get('confirm_password', '')

    # Validate: minimum 8 characters
    if len(new_password) < 8:
        flash('Password must be at least 8 characters long.', 'error')
        return render_template('change_password.html', forced=forced)

    # Validate: both fields must match
    if new_password != confirm_password:
        flash('Passwords do not match.', 'error')
        return render_template('change_password.html', forced=forced)

    # Hash the new password and update the database
    hashed = generate_password_hash(new_password, method='pbkdf2:sha256')
    db.execute('''UPDATE users
                  SET password = ?, must_change_password = 0
                  WHERE id = ?''',
               (hashed, session['user_id']))
    db.commit()

    flash('Password changed successfully.', 'success')
    return redirect(url_for('daily'))


# --- PKR Currency Formatter ---

def format_pkr(value):
    """Format a number as Rs. with South Asian lakh/crore commas.
    Example: 1952413 -> 'Rs. 19,52,413'
    """
    if value is None:
        value = 0

    negative = value < 0
    num = abs(value)
    s = str(num)

    if len(s) <= 3:
        formatted = s
    else:
        # Last 3 digits form the first group
        last3 = s[-3:]
        rest = s[:-3]
        # Group the remaining digits in pairs from right to left
        pairs = []
        while rest:
            pairs.append(rest[-2:])
            rest = rest[:-2]
        pairs.reverse()
        formatted = ','.join(pairs) + ',' + last3

    if negative:
        return f'Rs. -{formatted}'
    return f'Rs. {formatted}'


# Register as a Jinja template filter so templates can use {{ amount|pkr }}
app.jinja_env.filters['pkr'] = format_pkr


def format_monthday(date_str):
    """Format a date string (YYYY-MM-DD) as 'Apr 05'."""
    try:
        d = datetime.strptime(date_str, '%Y-%m-%d')
        return d.strftime('%b %d')
    except (ValueError, TypeError):
        return date_str


# Register as Jinja filter: {{ entry.date|monthday }}
