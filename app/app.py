# --- Database Helper ---
def get_db():
    """Get a database connection for the current request."""
    if 'db' not in g:
        g.db = sqlite3.connect(DATABASE)
        g.db.row_factory = sqlite3.Row
        # synchronous=NORMAL is per-connection and must be set here.
        # Safe with WAL mode; still fsyncs at checkpoint boundaries so
        # a crash cannot corrupt the database — only the very last
        # in-flight transaction can be lost. That trade is fine for a
        # single-user desktop accounting app.
        g.db.execute('PRAGMA synchronous = NORMAL')
    return g.db


@app.teardown_appcontext
def close_db(exception):
    """Close the database connection at the end of each request."""
    db = g.pop('db', None)
    if db is not None:
        db.close()


def init_db():
    """Create tables and run migrations if needed. Safe to call on every startup."""
    try:
        conn = sqlite3.connect(DATABASE)
        cursor = conn.cursor()

        # --- Create tables ---
        cursor.execute('''
            CREATE TABLE IF NOT EXISTS users (
                id                   INTEGER PRIMARY KEY AUTOINCREMENT,
                username             TEXT    NOT NULL UNIQUE,
                password             TEXT    NOT NULL,
                must_change_password INTEGER NOT NULL DEFAULT 1,
                failed_attempts      INTEGER NOT NULL DEFAULT 0,
                locked_until         TEXT    DEFAULT NULL
            )
        ''')

        cursor.execute('''
            CREATE TABLE IF NOT EXISTS daily_entries (
                id           INTEGER PRIMARY KEY AUTOINCREMENT,
                date         TEXT    NOT NULL,
                type         TEXT    NOT NULL,
                amount       INTEGER NOT NULL,
                description  TEXT    NOT NULL,
                notes        TEXT    DEFAULT '',
                is_deleted   INTEGER NOT NULL DEFAULT 0,
                created_at   TEXT    NOT NULL,
                payment_type TEXT    DEFAULT 'cash',
                receipt      TEXT,
                category     TEXT    DEFAULT 'other'
            )
        ''')

        cursor.execute('''
            CREATE TABLE IF NOT EXISTS settings (
                key   TEXT PRIMARY KEY,
                value TEXT NOT NULL
            )
        ''')

        # --- Migrate: add columns if missing (for databases created before these columns) ---
        cursor.execute('PRAGMA table_info(daily_entries)')
        existing_columns = [row[1] for row in cursor.fetchall()]

        if 'payment_type' not in existing_columns:
            cursor.execute("ALTER TABLE daily_entries ADD COLUMN payment_type TEXT DEFAULT 'cash'")

        if 'receipt' not in existing_columns:
            cursor.execute('ALTER TABLE daily_entries ADD COLUMN receipt TEXT')

        if 'category' not in existing_columns:
            cursor.execute("ALTER TABLE daily_entries ADD COLUMN category TEXT DEFAULT 'other'")

        # --- Indexes ---
        cursor.execute('''
            CREATE INDEX IF NOT EXISTS idx_entries_date
            ON daily_entries(date, is_deleted)
        ''')
        cursor.execute('''
            CREATE INDEX IF NOT EXISTS idx_entries_type
            ON daily_entries(date, type, is_deleted)
        ''')
        cursor.execute('''
            CREATE INDEX IF NOT EXISTS idx_entries_category
            ON daily_entries(category, is_deleted)
        ''')

        # --- Persistent PRAGMAs ---
        # WAL is a database-level setting — once set, it sticks across
        # restarts and is recorded in the DB file itself. Better write
        # concurrency, and readers never block writers. Safe on a local
        # Windows filesystem for a single-user desktop app.
        cursor.execute('PRAGMA journal_mode = WAL')

        # --- Default admin user if users table is empty ---
        cursor.execute('SELECT COUNT(*) FROM users')
        if cursor.fetchone()[0] == 0:
            hashed_password = generate_password_hash('admin123', method='pbkdf2:sha256')
            cursor.execute('''
                INSERT INTO users (username, password, must_change_password, failed_attempts, locked_until)
                VALUES (?, ?, 1, 0, NULL)
            ''', ('admin', hashed_password))

        # --- Default settings ---
        default_settings = [
            ('business_name', 'Waqar & Brothers'),
            ('opening_balance', '0'),
            ('fy_start', '2025-07-01'),
            ('fy_end', '2026-06-30'),
        ]
        for key, value in default_settings:
            cursor.execute('INSERT OR IGNORE INTO settings (key, value) VALUES (?, ?)', (key, value))

        conn.commit()
        conn.close()
    except Exception:
        # Log init errors to a file next to the executable/script
        log_path = os.path.join(BASE_PATH, 'init_error.log')
        with open(log_path, 'w') as f:
            f.write(traceback.format_exc())
        raise


