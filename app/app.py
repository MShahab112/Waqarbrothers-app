@app.route('/edit-entry/<int:entry_id>', methods=['GET', 'POST'])
@login_required
def edit_entry(entry_id):
    """Show edit form (GET) or save edited entry (POST)."""
    db = get_db()

    # Look up the entry (must exist and not be deleted)
    entry = db.execute('''SELECT id, date, type, amount, description,
                                 notes, payment_type, receipt, category,
                                 salesman_id, online_source, bank_name
                          FROM daily_entries
                          WHERE id = ? AND is_deleted = 0''',
                       (entry_id,)).fetchone()

    if entry is None:
        flash('Entry not found.', 'error')
        return redirect(url_for('daily'))

    # Block edits if the entry's own day is closed (GET or POST)
    if is_day_closed(entry['date']):
        flash('This day is closed. No changes allowed.', 'error')
        return redirect(url_for('daily', date=entry['date']))

    if request.method == 'GET':
        return render_template('edit_entry.html', entry=entry,
                               categories=CATEGORY_LABELS,
                               valid_categories=VALID_CATEGORIES,
                               online_sources=ONLINE_SOURCE_LABELS)

    # POST — save the edits
    entry_date = request.form.get('date', '').strip()
    description = request.form.get('description', '').strip()
    amount_str = request.form.get('amount', '').strip()
    notes = request.form.get('notes', '').strip()
    salesman_id_raw = request.form.get('salesman_id', '').strip()
    category_raw = request.form.get('category', '').strip().lower()
    online_source = request.form.get('online_source', '').strip().lower()
    bank_name = request.form.get('bank_name', '').strip()

    # Recovery entries must reference an existing active salesman.
    # No auto-create, no free-text fallback — same rules as add_entry.
    new_salesman_id = None
    if entry['type'] == 'recovery':
        try:
            sid = int(salesman_id_raw)
        except (ValueError, TypeError):
            sid = 0
        if sid <= 0:
            flash('Please select a valid salesman.', 'error')
            return render_template('edit_entry.html', entry=entry)

        sm_row = db.execute(
            'SELECT id, name, is_active FROM salesmen WHERE id = ?',
            (sid,)
        ).fetchone()
        if sm_row is None:
            flash('Please select a valid salesman.', 'error')
            return render_template('edit_entry.html', entry=entry)
        if not sm_row['is_active']:
            flash('That salesman is inactive. Reactivate it first from the Salesmen page.',
                  'error')
            return render_template('edit_entry.html', entry=entry)

        new_salesman_id = sm_row['id']
        description = sm_row['name']

    # Also block if user tries to move the entry into a closed day
    if is_day_closed(entry_date):
        flash('This day is closed. No changes allowed.', 'error')
        return redirect(url_for('daily', date=entry_date))

    # --- Validation (same rules as add) ---

    # Date must be valid
    try:
        parsed_date = date.fromisoformat(entry_date)
    except ValueError:
        flash('Invalid date format.', 'error')
        return render_template('edit_entry.html', entry=entry)

    # Date must not be in the future
    if parsed_date > date.today():
        flash('Future dates are not allowed.', 'error')
        return render_template('edit_entry.html', entry=entry)

    # Description is required (expense path only — recovery already validated above)
    if not description:
        if (request.form.get('category', '').strip().lower() == 'advance'):
            flash('Person name is required for Advance entries.', 'error')
        else:
            flash('Name / Purpose is required.', 'error')
        return render_template('edit_entry.html', entry=entry,
                               categories=CATEGORY_LABELS,
                               valid_categories=VALID_CATEGORIES)

    # Amount must be a positive whole number
    try:
        amount = int(amount_str)
    except (ValueError, TypeError):
        flash('Amount must be a valid number.', 'error')
        return render_template('edit_entry.html', entry=entry)

    if amount <= 0:
        flash('Amount must be greater than 0.', 'error')
        return render_template('edit_entry.html', entry=entry)

    # Category is user-selected for expenses — required, no silent fallback.
    # Recovery/online rows keep category NULL.
    if entry['type'] == 'expense':
        if category_raw not in VALID_CATEGORIES:
            flash('Please select a category for the expense.', 'error')
            return render_template('edit_entry.html', entry=entry,
                                   categories=CATEGORY_LABELS,
                                   valid_categories=VALID_CATEGORIES)
        new_category = category_raw
        # Same normalization as add_entry — keeps grouping consistent.
        description = normalize_expense_description(description)
        new_online_source = None
        new_bank_name = ''
    elif entry['type'] == 'online':
        if online_source not in ONLINE_SOURCE_VALUES:
            flash('Please select an online source.', 'error')
            return render_template('edit_entry.html', entry=entry,
                                   categories=CATEGORY_LABELS,
                                   valid_categories=VALID_CATEGORIES,
                                   online_sources=ONLINE_SOURCE_LABELS)
        if online_source == 'bank_transfer' and not bank_name:
            flash('Bank name is required for bank transfer source.', 'error')
            return render_template('edit_entry.html', entry=entry,
                                   categories=CATEGORY_LABELS,
                                   valid_categories=VALID_CATEGORIES,
                                   online_sources=ONLINE_SOURCE_LABELS)
        if online_source != 'bank_transfer':
            bank_name = ''
        source_label = ONLINE_SOURCE_LABELS.get(online_source, 'Online')
        description = source_label if not bank_name else f'{source_label} ({bank_name})'
        new_category = None
        new_online_source = online_source
        new_bank_name = bank_name
    else:
        new_category = entry['category']
        new_online_source = entry['online_source']
        new_bank_name = entry['bank_name'] or ''

    # Data integrity guard: block edits that would drive closing_balance
    # below zero. See the matching guard in add_entry() for the full
    # rationale on why the invariant uses opening+cash vs expense+bank and
    # why it only applies to expense edits.
    if entry['type'] == 'expense':
        new_payment_type = entry['payment_type'] or 'cash'
        opening = get_opening_balance(db, entry_date)
        base = get_day_cash_outflow(db, entry_date, exclude_entry_id=entry_id)
        d_rec, d_onl, d_exp, d_bank = _classify_entry_deltas(
            entry['type'], amount, new_category, new_payment_type
        )
        projected_cash = (base['recovery'] + d_rec) - (base['online'] + d_onl)
        projected_outflow = (base['expense'] + d_exp) + (base['bank_transfer'] + d_bank)
        projected_closing = opening + projected_cash - projected_outflow
        if projected_closing < 0:
            flash('Closing balance would go negative (overdraft).', 'error')
            return render_template('edit_entry.html', entry=entry,
                                   categories=CATEGORY_LABELS,
                                   valid_categories=VALID_CATEGORIES,
                                   online_sources=ONLINE_SOURCE_LABELS)
    elif entry['type'] == 'online':
        new_payment_type = 'online'
    else:
        new_payment_type = 'cash'

    # --- Update the entry ---
    if entry['type'] == 'recovery':
        db.execute('''UPDATE daily_entries
                      SET date = ?, description = ?, amount = ?, notes = ?,
                          category = ?, salesman_id = ?, online_source = ?, bank_name = ?
                      WHERE id = ?''',
                   (entry_date, description, amount, notes, new_category,
                    new_salesman_id, new_online_source, new_bank_name, entry_id))
    else:
        db.execute('''UPDATE daily_entries
                      SET date = ?, description = ?, amount = ?, notes = ?,
                          category = ?, online_source = ?, bank_name = ?
                      WHERE id = ?''',
                   (entry_date, description, amount, notes, new_category,
                    new_online_source, new_bank_name, entry_id))
    db.commit()
    _reports_cache.clear()

    app.logger.info(
        'Entry edited | id=%s date=%s category=%s amount=%s description=%r | %s',
        entry_id, entry_date, new_category or '-', amount, description, _log_ctx(),
    )
    flash('Entry updated.', 'success')
    return redirect(url_for('daily', date=entry_date))


