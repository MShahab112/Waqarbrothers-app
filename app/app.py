def day_backup_folder(date_str):
    """Return the date-specific backup subfolder, e.g. backups/backup-10-04-2026/"""
    folder_name = f"backup-{datetime.strptime(date_str, '%Y-%m-%d').strftime('%d-%m-%Y')}"
    return os.path.join(BACKUP_FOLDER, folder_name)


def is_day_closed(date_str):
    """Return True if a closed backup file exists for this date
    AND the day has not been re-opened."""
    if not date_str:
        return False
    try:
        folder = day_backup_folder(date_str)
        closed_file = os.path.join(folder, f'accounts_{date_str}_closed.db')
        reopened_file = os.path.join(folder, f'accounts_{date_str}_reopened.txt')
        return os.path.exists(closed_file) and not os.path.exists(reopened_file)
    except Exception:
        return False


def is_day_reopened(date_str):
    """Return True if a reopen marker file exists for this date."""
    if not date_str:
        return False
    try:
        folder = day_backup_folder(date_str)
        reopened_file = os.path.join(folder, f'accounts_{date_str}_reopened.txt')
        return os.path.exists(reopened_file)
    except Exception:
        return False


