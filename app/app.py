@app.route('/export/pdf')
@login_required
def export_pdf():
    """Export daily or monthly report as a downloadable PDF."""
    from reportlab.lib import colors
    from reportlab.platypus import Table, Paragraph, Spacer

    db = get_db()
    today = date.today()
    business_name = get_setting(db, 'business_name') or 'Waqar & Brothers'

    s = _pdf_styles()
    c = s['colors']
    entry_ts = _pdf_entry_table_style(c)

    date_param = request.args.get('date')
    month_param = request.args.get('month')
    year_param = request.args.get('year')

    elements = []

    if date_param:
        # --- DAILY REPORT ---
        try:
            report_date = date.fromisoformat(date_param)
        except ValueError:
            report_date = today
            date_param = today.isoformat()

        display = report_date.strftime('%d %B %Y')
        _pdf_header_block(elements, business_name,
                          f'Daily Report &mdash; {display}', None)

        opening_balance = get_opening_balance(db, date_param)

        # Opening balance card
        ob = Table([['Opening Balance', format_pkr(opening_balance)]],
                   colWidths=[330, 120])
        ob.setStyle(_pdf_totals_row_style(c['card_bg'], c['accent']))
        elements.append(ob)

        # INCOME section
        recoveries = db.execute('''
            SELECT description, amount
            FROM daily_entries
            WHERE date = ? AND type = 'recovery' AND is_deleted = 0
            ORDER BY id ASC
        ''', (date_param,)).fetchall()

        _pdf_section_heading(elements, 'Income (Recovery)', c, c['green'])

        if recoveries:
            data = [['#', 'Salesman', 'Amount']]
            for i, r in enumerate(recoveries, 1):
                data.append([str(i), r['description'], format_pkr(r['amount'])])
            t = Table(data, colWidths=[30, 300, 120])
            t.setStyle(entry_ts)
            elements.append(t)

            total_recovery = sum(r['amount'] for r in recoveries)
            elements.append(Spacer(1, 6))
            tt = Table([['Total Recovery', format_pkr(total_recovery)]],
                       colWidths=[330, 120])
            tt.setStyle(_pdf_totals_row_style(c['green_bg'], c['green_accent'],
                                               c['green']))
            elements.append(tt)
        else:
            elements.append(Paragraph('No recovery entries.', s['base']['Normal']))
            total_recovery = 0

        # EXPENSE section
        expenses = db.execute('''
            SELECT description, amount, payment_type
            FROM daily_entries
            WHERE date = ? AND type = 'expense' AND is_deleted = 0
            ORDER BY id ASC
        ''', (date_param,)).fetchall()

        _pdf_section_heading(elements, 'Expenses', c, c['red'])

        if expenses:
            data = [['#', 'Type', 'Description', 'Amount']]
            for i, e in enumerate(expenses, 1):
                ptype = (e['payment_type'] or 'cash').capitalize()
                data.append([str(i), ptype, e['description'],
                             format_pkr(e['amount'])])
            t = Table(data, colWidths=[30, 80, 220, 120])
            t.setStyle(entry_ts)
            elements.append(t)

            total_expenses = sum(e['amount'] for e in expenses)
            elements.append(Spacer(1, 6))
            tt = Table([['Total Expenses', format_pkr(total_expenses)]],
                       colWidths=[330, 120])
            tt.setStyle(_pdf_totals_row_style(c['red_bg'], c['red_accent'],
                                               c['red']))
            elements.append(tt)
        else:
            elements.append(Paragraph('No expense entries.', s['base']['Normal']))
            total_expenses = 0

        # SUMMARY
        net = opening_balance + total_recovery - total_expenses
        _pdf_section_heading(elements, 'Summary', c)
        summary_data = [
            ['Opening Balance', format_pkr(opening_balance)],
            ['+ Total Recovery', format_pkr(total_recovery)],
            ['\u2013  Total Expenses', format_pkr(total_expenses)],
            ['= Closing Balance', format_pkr(net)],
        ]
        elements.append(_pdf_summary_table(summary_data, c))

        _pdf_footer_block(elements)
        pdf_filename = f'WaqarBrothers_Daily_{date_param}.pdf'

    else:
        # --- MONTHLY REPORT ---
        try:
            month = int(month_param or today.month)
            year = int(year_param or today.year)
            if not (1 <= month <= 12):
                raise ValueError
        except (ValueError, TypeError):
            month = today.month
            year = today.year

        month_str = str(month).zfill(2)
        year_str = str(year)
        month_label = date(year, month, 1).strftime('%B %Y')

        _pdf_header_block(elements, business_name,
                          f'Monthly Report &mdash; {month_label}', None)

        # Opening balance for the month
        first_of_month = f'{year_str}-{month_str}-01'
        opening_balance = get_opening_balance(db, first_of_month)

        # Fetch all entries for the month grouped by day
        rows = db.execute('''
            SELECT date, type, description, amount, payment_type
            FROM daily_entries
            WHERE strftime('%m', date) = ? AND strftime('%Y', date) = ?
                  AND is_deleted = 0
            ORDER BY date ASC, id ASC
        ''', (month_str, year_str)).fetchall()

        total_recovery = 0
        total_expense = 0

        if rows:
            data = [['Date', 'Type', 'Description', 'Payment', 'Amount']]
            for row in rows:
                ptype = (row['payment_type'] or 'cash').capitalize()
                entry_type = row['type'].capitalize()
                data.append([
                    row['date'],
                    entry_type,
                    row['description'],
                    ptype,
                    format_pkr(row['amount']),
                ])
                if row['type'] == 'recovery':
                    total_recovery += row['amount']
                else:
                    total_expense += row['amount']

            t = Table(data, colWidths=[70, 65, 170, 65, 100])
            t.setStyle(entry_ts)
            elements.append(t)
        else:
            elements.append(Paragraph('No entries for this month.',
                                       s['base']['Normal']))

        # SUMMARY
        net = opening_balance + total_recovery - total_expense
        _pdf_section_heading(elements, 'Summary', c)
        summary_data = [
            ['Opening Balance', format_pkr(opening_balance)],
            ['+ Total Recovery', format_pkr(total_recovery)],
            ['\u2013  Total Expenses', format_pkr(total_expense)],
            ['= Closing Balance', format_pkr(net)],
        ]
        elements.append(_pdf_summary_table(summary_data, c))

        _pdf_footer_block(elements)
        month_short = date(year, month, 1).strftime('%b')
        pdf_filename = f'WaqarBrothers_{month_short}_{year}.pdf'

    output = _build_pdf_document(elements)

    return send_file(output,
                     as_attachment=True,
                     download_name=pdf_filename,
                     mimetype='application/pdf')


