def get_db():
    """Get a database connection for the current request."""
    if 'db' not in g:
        g.db = sqlite3.connect(DATABASE)
        g.db.row_factory = sqlite3.Row
        # synchronous=NORMAL is per-connection and must be set here.
        # Safe with WAL mode; still fsyncs at checkpoint boundaries so
        # a crash cannot corrupt the database — only the very last
        # in-flight transaction can be lost. That trade is fine for a
        # single-user desktop accounting app.
        g.db.execute('PRAGMA synchronous = NORMAL')
    return g.db


