            self.drawString(18*mm, 9*mm, generated_line)
            self.drawCentredString(w / 2, 9*mm, brand_line)
            # Page X of Y (right)
            self.drawRightString(
                w - 18*mm, 9*mm,
                f'Page {self._pageNumber} of {total_pages}')

