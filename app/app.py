class _NumberedCanvas:
    """Canvas wrapper that renders 'Page X of Y' on every page."""

    @staticmethod
    def make(brand_hex='#5d6d7e'):
        from reportlab.pdfgen import canvas as pdf_canvas
        from reportlab.lib.units import mm
        from reportlab.lib import colors

        color = colors.HexColor(brand_hex)

        class Numbered(pdf_canvas.Canvas):
            def __init__(self, *args, **kwargs):
                pdf_canvas.Canvas.__init__(self, *args, **kwargs)
                self._saved_page_states = []

            def showPage(self):
                self._saved_page_states.append(dict(self.__dict__))
                self._startPage()

            def save(self):
                total = len(self._saved_page_states)
                for state in self._saved_page_states:
                    self.__dict__.update(state)
                    self._draw_watermark()
                    self._draw_page_number(total)
                    pdf_canvas.Canvas.showPage(self)
                pdf_canvas.Canvas.save(self)

            def _draw_watermark(self):
                self.saveState()
                self.setFont('Helvetica-Bold', 48)
                self.setFillColorRGB(0.92, 0.93, 0.95, alpha=0.35)
                w, h = self._pagesize
                self.translate(w / 2, h / 2)
                self.rotate(45)
                self.drawCentredString(0, 0, 'Waqar Brothers')
                self.restoreState()

            def _draw_page_number(self, total):
                self.setFont('Helvetica', 8)
                self.setFillColor(color)
                self.drawRightString(
                    self._pagesize[0] - 20*mm, 10*mm,
                    f'Page {self._pageNumber} of {total}')

        return Numbered


def _build_pdf_document(elements):
    """Build a ReportLab PDF in memory and return BytesIO at position 0."""
    from reportlab.lib.pagesizes import A4
    from reportlab.lib.units import mm
    from reportlab.platypus import SimpleDocTemplate

    output = io.BytesIO()
    doc = SimpleDocTemplate(output, pagesize=A4,
                             leftMargin=20*mm, rightMargin=20*mm,
                             topMargin=18*mm, bottomMargin=18*mm)
    doc.build(elements, canvasmaker=_NumberedCanvas.make())
    output.seek(0)
    return output


def _build_daily_pdf_bytes(db, date_str):
    """Build a Daily Closing Report PDF for the given date.
    Returns a BytesIO positioned at 0. Raises on failure.

    This is a small helper for Close Day — it is independent of the
    existing /export/pdf route so that route's logic stays untouched.
    """
    from reportlab.lib import colors
    from reportlab.platypus import Table, Paragraph, Spacer

    s = _pdf_styles()
    c = s['colors']

    report_date = date.fromisoformat(date_str)
    business_name = get_setting(db, 'business_name') or 'Waqar & Brothers'
    opening_balance = get_opening_balance(db, date_str)

    recoveries = db.execute('''
        SELECT description, amount
        FROM daily_entries
        WHERE date = ? AND type = 'recovery' AND is_deleted = 0
        ORDER BY id ASC
    ''', (date_str,)).fetchall()

    expenses = db.execute('''
        SELECT description, amount
        FROM daily_entries
        WHERE date = ? AND type = 'expense' AND is_deleted = 0
        ORDER BY id ASC
    ''', (date_str,)).fetchall()

    total_recovery = sum(r['amount'] for r in recoveries)
    total_expenses = sum(e['amount'] for e in expenses)
    closing_balance = opening_balance + total_recovery - total_expenses

    entry_ts = _pdf_entry_table_style(c)

    elements = []

    _pdf_header_block(elements, business_name,
                      f'Daily Closing Report &mdash; {report_date.strftime("%d %B %Y")}',
                      None)

    # Opening balance card
    ob_data = [['Opening Balance', format_pkr(opening_balance)]]
    ob = Table(ob_data, colWidths=[330, 120])
    ob.setStyle(_pdf_totals_row_style(c['card_bg'], c['accent']))
    elements.append(ob)
    elements.append(Spacer(1, 10))

    # Recovery section
    _pdf_section_heading(elements, 'Recovery Entries', c, c['green'])
    if recoveries:
        data = [['#', 'Salesman', 'Amount']]
        for i, r in enumerate(recoveries, 1):
            data.append([str(i), r['description'], format_pkr(r['amount'])])
        t = Table(data, colWidths=[30, 300, 120])
        t.setStyle(entry_ts)
        elements.append(t)
    else:
        elements.append(Paragraph('No recovery entries for this day.',
                                   s['base']['Normal']))
    elements.append(Spacer(1, 6))
    tr = Table([['Total Recovery', format_pkr(total_recovery)]],
               colWidths=[330, 120])
    tr.setStyle(_pdf_totals_row_style(c['green_bg'], c['green_accent'],
                                       c['green']))
    elements.append(tr)
    elements.append(Spacer(1, 10))

    # Expense section
    _pdf_section_heading(elements, 'Expense Entries', c, c['red'])
    if expenses:
        data = [['#', 'Description', 'Amount']]
        for i, ex in enumerate(expenses, 1):
            data.append([str(i), ex['description'], format_pkr(ex['amount'])])
        t = Table(data, colWidths=[30, 300, 120])
        t.setStyle(entry_ts)
        elements.append(t)
    else:
        elements.append(Paragraph('No expense entries for this day.',
                                   s['base']['Normal']))
    elements.append(Spacer(1, 6))
    te = Table([['Total Expenses', format_pkr(total_expenses)]],
               colWidths=[330, 120])
    te.setStyle(_pdf_totals_row_style(c['red_bg'], c['red_accent'],
                                       c['red']))
    elements.append(te)
    elements.append(Spacer(1, 12))

    # Summary section
    _pdf_section_heading(elements, 'Summary', c)
    summary_data = [
        ['Opening Balance', format_pkr(opening_balance)],
        ['+ Total Recovery', format_pkr(total_recovery)],
        ['\u2013  Total Expenses', format_pkr(total_expenses)],
        ['= Closing Balance', format_pkr(closing_balance)],
    ]
    elements.append(_pdf_summary_table(summary_data, c))

    closed_at = datetime.now().strftime('%d %B %Y, %I:%M %p').lstrip('0')
    elements.append(Spacer(1, 10))
    _pdf_footer_block(elements, extra_line=f'Day closed on {closed_at}')

    return _build_pdf_document(elements)


@app.route('/close-day', methods=['POST'])
@login_required
def close_day():
    db = get_db()
    date_str = request.form.get('date', '').strip()

    if not date_str:
        flash('Invalid date.', 'error')
        return redirect(url_for('daily'))

    # Validate date format
    try:
        date.fromisoformat(date_str)
    except ValueError:
        flash('Invalid date format.', 'error')
        return redirect(url_for('daily'))

    # Ensure date-specific backup folder exists
    day_folder = day_backup_folder(date_str)
    try:
        os.makedirs(day_folder, exist_ok=True)
    except Exception as e:
        flash(f'Could not create backup folder: {e}', 'error')
        return redirect(url_for('daily', date=date_str))

    # Check if already closed (but allow re-close if the day was reopened)
    closed_file = os.path.join(day_folder, f'accounts_{date_str}_closed.db')
    reopened_file = os.path.join(
        day_folder, f'accounts_{date_str}_reopened.txt')

    if os.path.exists(closed_file) and not os.path.exists(reopened_file):
        flash('Day already closed.', 'error')
        return redirect(url_for('daily', date=date_str))

    # If the day had been reopened, clear the reopen marker so the fresh
    # close takes effect. The existing closed.db is simply overwritten
    # below — we never touch historical backups besides this same file.
    if os.path.exists(reopened_file):
        try:
            os.remove(reopened_file)
        except Exception as e:
            flash(f'Could not clear reopen marker: {e}', 'error')
            return redirect(url_for('daily', date=date_str))

    # STEP 1 — create (or overwrite) the closed DB backup
    try:
        shutil.copy2(DATABASE, closed_file)
    except Exception as e:
        flash(f'Error closing day: {str(e)}', 'error')
        return redirect(url_for('daily', date=date_str))

    # STEP 2 — build PDF report (reuses shared helper)
    try:
        pdf_buffer = _build_daily_pdf_bytes(db, date_str)
    except Exception as e:
        flash(f'Day closed but PDF generation failed: {str(e)}', 'error')
        return redirect(url_for('daily', date=date_str))

    # STEP 3 — save PDF to date-specific backup folder
    pdf_filename = f'daily_report_{date_str}.pdf'
    pdf_path = os.path.join(day_folder, pdf_filename)
    try:
        with open(pdf_path, 'wb') as f:
            f.write(pdf_buffer.getvalue())
    except Exception as e:
        # Not fatal — the DB backup is the critical artifact; still download
        flash(f'PDF download ready, but saving to backups failed: {str(e)}',
              'error')

    # STEP 4 — send PDF as download
    pdf_buffer.seek(0)
    return send_file(pdf_buffer,
                     as_attachment=True,
                     download_name=pdf_filename,
                     mimetype='application/pdf')


@app.route('/reopen-day', methods=['POST'])
@login_required
def reopen_day():
    """Re-open a previously closed day so entries can be edited again.
    Does NOT delete or modify the existing closed backup file.
    Creates a marker file accounts_<date>_reopened.txt which is_day_closed()
    checks to decide the editable state."""
    db = get_db()
    date_str = request.form.get('date', '').strip()
    pin = request.form.get('pin', '').strip()

    if not date_str:
        flash('Invalid date.', 'error')
        return redirect(url_for('daily'))

    # Validate date format
    try:
        date.fromisoformat(date_str)
    except ValueError:
        flash('Invalid date format.', 'error')
        return redirect(url_for('daily'))

    # Require a valid 4-digit admin PIN before doing anything else
    if not verify_admin_pin(db, pin):
        flash('Invalid PIN.', 'error')
        return redirect(url_for('daily', date=date_str))

    # Ensure date-specific backup folder exists
    day_folder = day_backup_folder(date_str)
    try:
        os.makedirs(day_folder, exist_ok=True)
    except Exception as e:
        flash(f'Could not access backup folder: {e}', 'error')
        return redirect(url_for('daily', date=date_str))

    # Must have been closed before it can be reopened
    closed_file = os.path.join(
        day_folder, f'accounts_{date_str}_closed.db')
    if not os.path.exists(closed_file):
        flash('This day is not closed, so it cannot be reopened.', 'error')
        return redirect(url_for('daily', date=date_str))

    # Create reopened marker file (safe — never touches the closed.db backup)
    reopened_file = os.path.join(
        day_folder, f'accounts_{date_str}_reopened.txt')

    # Block duplicate reopens
    if os.path.exists(reopened_file):
        flash('Day already reopened.', 'error')
        return redirect(url_for('daily', date=date_str))

    try:
        timestamp = datetime.now().strftime('%Y-%m-%d %I:%M %p')
        user_label = session.get('username', 'unknown')
        with open(reopened_file, 'w', encoding='utf-8') as f:
            f.write(f'Day {date_str} reopened on {timestamp} by {user_label}\n')
    except Exception as e:
        flash(f'Error reopening day: {str(e)}', 'error')
        return redirect(url_for('daily', date=date_str))

    # Append to reopen log
    try:
        logs_folder = os.path.join(BASE_DIR, 'logs')
        os.makedirs(logs_folder, exist_ok=True)
        log_path = os.path.join(logs_folder, 'reopen.log')
        log_stamp = datetime.now().strftime('%Y-%m-%d %I:%M %p')
        with open(log_path, 'a', encoding='utf-8') as f:
            f.write(f'{log_stamp} — Day {date_str} reopened by '
                    f'{session.get("username", "unknown")}\n')
    except Exception:
        # Logging failure must not block the reopen action
        pass

    flash('Day reopened successfully.', 'success')
    return redirect(url_for('daily', date=date_str))


@app.route('/change-pin', methods=['POST'])
@login_required
def change_pin():
    """Change the admin PIN used to authorize Re-open Day.
    Requires the logged-in user's own login password as re-auth —
    this collapses the original two-step flow into one secure POST."""
    db = get_db()
    current_password = request.form.get('current_password', '')
    new_pin = request.form.get('new_pin', '').strip()
    confirm_pin = request.form.get('confirm_pin', '').strip()

    # Re-verify the logged-in user's password
    user = db.execute('SELECT password FROM users WHERE id = ?',
                      (session.get('user_id'),)).fetchone()
    if not user or not check_password_hash(user['password'], current_password):
        flash('Incorrect login password.', 'error')
        return redirect(url_for('settings'))

    # Validate the new PIN
    if not (new_pin.isdigit() and len(new_pin) == 4):
        flash('PIN must be exactly 4 digits.', 'error')
        return redirect(url_for('settings'))

    if new_pin != confirm_pin:
        flash('PINs do not match.', 'error')
        return redirect(url_for('settings'))

    set_admin_pin(db, new_pin)
    flash('PIN updated successfully.', 'success')
    return redirect(url_for('settings'))


# --- Monthly Summary PDF (clean, minimal B/W style) ---

def _build_monthly_pdf_bytes(db, year, month):
    """Build the clean monthly summary PDF for the given year/month.
    Uses the same report data as /reports — no duplicated computation.

    Design rules (per UX spec): white background, black text, subtle gray
    borders, bold section headings, simple table grids, A4 portrait.
    Returns a BytesIO positioned at 0.
    """
    from calendar import monthrange
    from reportlab.lib import colors
    from reportlab.lib.pagesizes import A4
    from reportlab.lib.units import mm
    from reportlab.lib.styles import ParagraphStyle, getSampleStyleSheet
    from reportlab.lib.enums import TA_CENTER, TA_RIGHT, TA_LEFT
    from reportlab.platypus import (
        SimpleDocTemplate, Table, TableStyle, Paragraph, Spacer)

    # Date range for the month (clamp end to today for the current month)
    last_day = monthrange(year, month)[1]
    first = date(year, month, 1)
    last = date(year, month, last_day)
    today = date.today()
    if last > today:
        last = today

    date_from = first.isoformat()
    date_to = last.isoformat()

    business_name = get_setting(db, 'business_name') or 'Waqar & Brothers'
    data = _compute_reports_data(db, business_name, date_from, date_to)

    # Minimal monochrome palette
    BLACK     = colors.HexColor('#000000')
    TEXT      = colors.HexColor('#111111')
    MUTED     = colors.HexColor('#555555')
    GRID      = colors.HexColor('#BBBBBB')
    GRID_LT   = colors.HexColor('#DDDDDD')
    HEADER_BG = colors.HexColor('#F5F5F5')

    base = getSampleStyleSheet()
    title_style = ParagraphStyle(
        'MT', parent=base['Normal'],
        fontName='Helvetica-Bold', fontSize=20, leading=24,
        textColor=BLACK, alignment=TA_CENTER, spaceAfter=2)
    subtitle_style = ParagraphStyle(
        'MSub', parent=base['Normal'],
        fontName='Helvetica-Bold', fontSize=13, leading=16,
        textColor=TEXT, alignment=TA_CENTER, spaceAfter=2)
    period_style = ParagraphStyle(
        'MPeriod', parent=base['Normal'],
        fontName='Helvetica', fontSize=11, leading=14,
        textColor=MUTED, alignment=TA_CENTER, spaceAfter=4)
    section_style = ParagraphStyle(
        'MSection', parent=base['Normal'],
        fontName='Helvetica-Bold', fontSize=13, leading=17,
        textColor=BLACK, alignment=TA_LEFT, spaceBefore=18, spaceAfter=8)
    empty_style = ParagraphStyle(
        'Empty', parent=base['Normal'],
        fontName='Helvetica-Oblique', fontSize=10, textColor=MUTED,
        spaceAfter=4)

    def plain_table_style(header_row=True):
        cmds = [
            ('FONTNAME', (0, 0), (-1, -1), 'Helvetica'),
            ('FONTSIZE', (0, 0), (-1, -1), 10),
            ('TEXTCOLOR', (0, 0), (-1, -1), TEXT),
            ('VALIGN', (0, 0), (-1, -1), 'MIDDLE'),
            ('LEFTPADDING', (0, 0), (-1, -1), 10),
            ('RIGHTPADDING', (0, 0), (-1, -1), 10),
            ('TOPPADDING', (0, 0), (-1, -1), 9),
            ('BOTTOMPADDING', (0, 0), (-1, -1), 9),
            ('GRID', (0, 0), (-1, -1), 0.4, GRID_LT),
            ('BOX', (0, 0), (-1, -1), 0.6, GRID),
            ('ALIGN', (-1, 0), (-1, -1), 'RIGHT'),
        ]
        if header_row:
            cmds += [
                ('BACKGROUND', (0, 0), (-1, 0), HEADER_BG),
                ('FONTNAME', (0, 0), (-1, 0), 'Helvetica-Bold'),
                ('FONTSIZE', (0, 0), (-1, 0), 10.5),
                ('TEXTCOLOR', (0, 0), (-1, 0), BLACK),
                ('LINEBELOW', (0, 0), (-1, 0), 0.8, BLACK),
                ('TOPPADDING', (0, 0), (-1, 0), 10),
                ('BOTTOMPADDING', (0, 0), (-1, 0), 10),
            ]
        return TableStyle(cmds)

    elements = []

    # Header: business name (large) + "Monthly Financial Report" + period
    elements.append(Paragraph(business_name, title_style))
    elements.append(Paragraph('Monthly Financial Report', subtitle_style))
    elements.append(Paragraph(first.strftime('%B %Y'), period_style))

    # Thin rule under header
    rule = Table([['']], colWidths=[174*mm])
    rule.setStyle(TableStyle([
        ('LINEBELOW', (0, 0), (-1, 0), 0.6, BLACK),
        ('TOPPADDING', (0, 0), (-1, -1), 0),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 0),
    ]))
    elements.append(Spacer(1, 6))
    elements.append(rule)

    # Summary — last row (Closing Balance) rendered bold + larger
    elements.append(Paragraph('Summary', section_style))
    summary = [
        ['Item', 'Amount (PKR)'],
        ['Opening Balance',  format_pkr(data['period_opening'])],
        ['Total Recovery',   format_pkr(data['total_recovery'])],
        ['Total Expenses',   format_pkr(data['total_expense'])],
        ['Bank Transfer',    format_pkr(data['total_bank_transfer'])],
        ['Closing Balance',  format_pkr(data['closing_balance'])],
    ]
    st = Table(summary, colWidths=[114*mm, 60*mm])
    style = plain_table_style(header_row=True)
    # Closing Balance row: bold + slightly larger, with a top rule
    style.add('FONTNAME', (0, -1), (-1, -1), 'Helvetica-Bold')
    style.add('FONTSIZE', (0, -1), (-1, -1), 12)
    style.add('TOPPADDING', (0, -1), (-1, -1), 11)
    style.add('BOTTOMPADDING', (0, -1), (-1, -1), 11)
    style.add('LINEABOVE', (0, -1), (-1, -1), 0.8, BLACK)
    # Also bold the Total Recovery and Total Expenses rows
    style.add('FONTNAME', (0, 2), (-1, 2), 'Helvetica-Bold')
    style.add('FONTNAME', (0, 3), (-1, 3), 'Helvetica-Bold')
    st.setStyle(style)
    elements.append(st)

    # Expense by Category
    elements.append(Paragraph('Expense by Category', section_style))
    if data['category_totals']:
        rows = [['Category', 'Amount (PKR)']]
        for cat in data['category_totals']:
            rows.append([cat['name'], format_pkr(cat['amount'])])
        tcat = Table(rows, colWidths=[114*mm, 60*mm])
        tcat.setStyle(plain_table_style(header_row=True))
        elements.append(tcat)
    else:
        elements.append(Paragraph('No expenses recorded this month.', empty_style))

    # Top Salesmen
    elements.append(Paragraph('Top Salesmen', section_style))
    top3 = data['top_salesmen'][:3]
    if top3:
        rows = [['Rank', 'Salesman', 'Total Recovery (PKR)']]
        for i, sm in enumerate(top3, start=1):
            rows.append([str(i), sm['name'], format_pkr(sm['total'])])
        tsm = Table(rows, colWidths=[22*mm, 92*mm, 60*mm])
        tsm_style = plain_table_style(header_row=True)
        tsm_style.add('ALIGN', (0, 0), (0, -1), 'CENTER')
        tsm.setStyle(tsm_style)
        elements.append(tsm)
    else:
        elements.append(Paragraph('No recovery recorded this month.', empty_style))

    # Daily Summary
    elements.append(Paragraph('Daily Summary', section_style))
    if data['daily_data']:
        rows = [['Date', 'Recovery (PKR)', 'Expense (PKR)', 'Closing (PKR)']]
        # daily_data is newest-first; show oldest-first in the PDF
        for d in reversed(data['daily_data']):
            rows.append([
                d['date'],
                format_pkr(d['recovery']),
                format_pkr(d['expense']),
                format_pkr(d['balance']),
            ])
        tdaily = Table(rows, colWidths=[36*mm, 46*mm, 46*mm, 46*mm],
                       repeatRows=1)
        tdaily_style = plain_table_style(header_row=True)
        tdaily_style.add('ALIGN', (1, 0), (-1, -1), 'RIGHT')
        tdaily_style.add('ALIGN', (0, 0), (0, -1), 'CENTER')
        tdaily.setStyle(tdaily_style)
        elements.append(tdaily)
    else:
        elements.append(Paragraph('No entries recorded this month.', empty_style))

    # Canvas that draws timestamp (bottom-left) and "Page X of Y" (bottom-right)
    # on every page. This is the minimal equivalent of the styled NumberedCanvas.
    gen_time = datetime.now().strftime('%d %b %Y, %I:%M %p').lstrip('0')
    generated_line = f'Generated on: {gen_time}'
    brand_line = business_name

    from reportlab.pdfgen import canvas as _pdf_canvas

    class MonoFooterCanvas(_pdf_canvas.Canvas):
        def __init__(self, *args, **kwargs):
            super().__init__(*args, **kwargs)
            self._saved_page_states = []

        def showPage(self):
            self._saved_page_states.append(dict(self.__dict__))
            self._startPage()

        def save(self):
            total = len(self._saved_page_states)
            for state in self._saved_page_states:
                self.__dict__.update(state)
                self._draw_footer(total)
                super().showPage()
            super().save()

        def _draw_footer(self, total_pages):
            w, h = self._pagesize
            # Footer top rule
            self.setStrokeColor(GRID_LT)
            self.setLineWidth(0.4)
            self.line(18*mm, 14*mm, w - 18*mm, 14*mm)
            # Timestamp (left) and brand (center)
            self.setFont('Helvetica', 8)
            self.setFillColor(MUTED)
            self.drawString(18*mm, 9*mm, generated_line)
            self.drawCentredString(w / 2, 9*mm, brand_line)
            # Page X of Y (right)
            self.drawRightString(
                w - 18*mm, 9*mm,
                f'Page {self._pageNumber} of {total_pages}')

    output = io.BytesIO()
    doc = SimpleDocTemplate(
        output, pagesize=A4,
        leftMargin=18*mm, rightMargin=18*mm,
        topMargin=18*mm, bottomMargin=22*mm,
        title=f'Monthly Financial Report - {first.strftime("%B %Y")}',
        author=business_name)
    doc.build(elements, canvasmaker=MonoFooterCanvas)
    output.seek(0)
    return output


