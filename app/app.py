@app.errorhandler(Exception)
def handle_exception(e):
    """Log the full traceback to app.log and show a safe error page.

    Every unhandled exception gets a short unique ID that's logged
    alongside the traceback AND shown to the user, so a bug report
    ('Error ID abc12345') can be matched to the exact line in app.log.
    We never leak tracebacks to the browser. HTTP exceptions
    (404, 413, etc.) are re-raised so Flask's native handlers keep
    their behavior.
    """
    from werkzeug.exceptions import HTTPException
    if isinstance(e, HTTPException):
        return e
    error_id = uuid.uuid4().hex[:8]
    app.logger.error('[%s] Unhandled exception: %s | %s',
                     error_id, e, _log_ctx(), exc_info=True)
    # Not flashing here: we render error.html directly (not redirect), and
    # base.html's flash container would consume the message on the error
    # page itself — duplicating the ID that's already shown inline. The
    # flash pattern fits routes that catch + redirect, not this global trap.
    try:
        return render_template('error.html', error_id=error_id), 500
    except Exception:
        # Absolute last resort — the error template itself failed.
        return ('<h2>Something went wrong</h2>'
                f'<p>Please restart the app or contact support. Error ID: {error_id}</p>'), 500


