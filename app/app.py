def get_reopen_time(date_str):
    """Return the time-of-day of the most recent reopen for this date,
    as a human string like '08:45 PM', by parsing logs/reopen.log.
    Returns None if nothing is found."""
    if not date_str:
        return None
    log_file = os.path.join(BASE_DIR, 'logs', 'reopen.log')
    if not os.path.exists(log_file):
        return None
    try:
        with open(log_file, 'r', encoding='utf-8') as f:
            lines = f.readlines()

        # Log line format (written by reopen_day):
        #   2026-04-09 08:45 PM — Day 2026-04-09 reopened by admin
        marker = f'Day {date_str} reopened'
        for line in reversed(lines):
            if marker not in line:
                continue
            stamp = line.strip().split(' — ', 1)[0]
            # stamp = "2026-04-09 08:45 PM"  →  "08:45 PM"
            parts = stamp.split(' ')
            if len(parts) >= 3:
                return ' '.join(parts[1:])
            return stamp
    except Exception:
        return None
    return None


def validate_database(file_path):
    """Check that file_path is a valid SQLite DB with all required tables.
    Returns (True, '') on success or (False, reason) on failure."""
    try:
        conn = sqlite3.connect(file_path)
        cursor = conn.cursor()

        # Step 1: SQLite integrity check
        result = cursor.execute('PRAGMA integrity_check').fetchone()
        if result[0] != 'ok':
            conn.close()
            return False, 'Database integrity check failed.'

        # Step 2: Verify required tables exist
        cursor.execute(
            "SELECT name FROM sqlite_master WHERE type='table' AND name IN "
            "('users', 'daily_entries', 'settings')"
        )
        found = {row[0] for row in cursor.fetchall()}
        missing = {'users', 'daily_entries', 'settings'} - found
        conn.close()

        if missing:
            return False, f"Missing tables: {', '.join(sorted(missing))}"

        return True, ''
    except sqlite3.DatabaseError as e:
        return False, f'Not a valid SQLite database: {e}'


