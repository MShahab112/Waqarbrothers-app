@app.route('/expenses/details')
@login_required
def expense_details():
    """Drill-down view for the dashboard Expense / Bank Transfer cards.
    Shows category totals, payment-type totals, and the full entry list
    filtered by date range. Read-only — no mutations."""
    db = get_db()
    business_name = get_setting(db, 'business_name') or 'Waqar & Brothers'

    today = date.today()
    month_start = today.replace(day=1)

    # Date range — defaults to current month if not supplied.
    date_from = (request.args.get('from') or month_start.isoformat()).strip()
    date_to = (request.args.get('to') or today.isoformat()).strip()

    # Which card the user came from. Used only for heading emphasis —
    # the page always shows both breakdowns and the full entry list.
    view_type = request.args.get('type', 'category').strip()
    if view_type not in ('category', 'payment'):
        view_type = 'category'

    # Totals come from a grouped aggregate over the FULL range. Per business
    # rule 4, bank_slip is a cash-to-bank transfer, not an expense, so it's
    # excluded from category + expense totals but reported separately.
    total_expense = 0
    total_bank_transfer = 0
    category_totals = {}
    payment_totals = {'cash': 0, 'bank_slip': 0}
    total_rows = 0
    for r in execute_timed(db, '''
        SELECT COALESCE(category, 'other')       AS cat,
               TRIM(description)                 AS desc,
               COALESCE(payment_type, 'cash')    AS pt,
               COALESCE(SUM(amount), 0)          AS total,
               COUNT(*)                          AS n
        FROM daily_entries
        WHERE type = 'expense'
          AND is_deleted = 0
          AND date >= ? AND date <= ?
        GROUP BY cat, desc, pt
    ''', (date_from, date_to)).fetchall():
        pt = r['pt'] if r['pt'] in payment_totals else 'cash'
        payment_totals[pt] += r['total']
        total_rows += r['n']
        if is_bank_transfer_row(r['cat'], pt):
            total_bank_transfer += r['total']
        else:
            total_expense += r['total']
            if r['cat'] == 'advance':
                key = 'advance'
            else:
                key = (r['desc'] or 'Other').title()
            category_totals[key] = category_totals.get(key, 0) + r['total']

    # Hard cap on the entry list to keep the page responsive on large ranges.
    # Summary cards above still reflect the full date range.
    _ROW_LIMIT = 1000
    truncated = total_rows > _ROW_LIMIT
    rows = execute_timed(db, '''
        SELECT id, date, description, notes,
               COALESCE(category, 'other')       AS category,
               COALESCE(payment_type, 'cash')    AS payment_type,
               amount
        FROM daily_entries
        WHERE type = 'expense'
          AND is_deleted = 0
          AND date >= ? AND date <= ?
        ORDER BY date DESC, id DESC
        LIMIT ?
    ''', (date_from, date_to, _ROW_LIMIT)).fetchall()

    category_list = sorted(
        [{'key': k,
          'label': _CATEGORY_LABELS.get(k, k.title()),
          'total': v}
         for k, v in category_totals.items()],
        key=lambda x: -x['total'],
    )

    payment_list = [
        {'key': k,
         'label': _PAYMENT_LABELS.get(k, k.title()),
         'total': payment_totals.get(k, 0)}
        for k in ('cash', 'bank_slip')
    ]

    # Quick-filter anchors.
    last_month_end = month_start - timedelta(days=1)
    last_month_start = last_month_end.replace(day=1)

    # Enrich rows for the template (JSON-safe dicts + pretty labels).
    entries = []
    for r in rows:
        entries.append({
            'id':            r['id'],
            'date':          r['date'],
            'description':   r['description'],
            'notes':         r['notes'],
            'category':      r['category'],
            'category_label': _CATEGORY_LABELS.get(r['category'], r['category'].title()),
            'payment_type':  r['payment_type'],
            'payment_label': _PAYMENT_LABELS.get(r['payment_type'], r['payment_type'].title()),
            'amount':        int(r['amount'] or 0),
        })

    return render_template('expense_details.html',
                           business_name=business_name,
                           date_from=date_from,
                           date_to=date_to,
                           view_type=view_type,
                           entries=entries,
                           total_expense=total_expense,
                           total_bank_transfer=total_bank_transfer,
                           payment_list=payment_list,
                           category_list=category_list,
                           total_rows=total_rows,
                           row_limit=_ROW_LIMIT,
                           truncated=truncated,
                           today_iso=today.isoformat(),
                           month_start_iso=month_start.isoformat(),
                           last_month_start_iso=last_month_start.isoformat(),
                           last_month_end_iso=last_month_end.isoformat())


@app.route('/reports')
@login_required
def reports():
    """Show daily summaries with recovery, expense, and closing balance.
    Caching happens inside _compute_reports_data (8s TTL, cleared on mutation)."""
    date_from = request.args.get('from', '').strip()
    date_to = request.args.get('to', '').strip()
    desc_param = request.args.get('description', '').strip()
    cat_param = request.args.get('category', '').strip()
    type_filter = request.args.get('type_filter', 'all').strip().lower()
    online_source_filter = request.args.get('online_source', 'all').strip().lower()
    # Salesman filter — numeric id or empty/"all". Narrows Recovery in the
    # daily table + Overview to just that salesman's collections so the user
    # can see per-salesman performance without leaving /reports.
    salesman_id_raw = (request.args.get('salesman_id', '') or '').strip()
    salesman_id_filter = 0
    if salesman_id_raw.isdigit():
        salesman_id_filter = int(salesman_id_raw)
    if type_filter not in VALID_TYPE_FILTERS:
        type_filter = 'all'
    if online_source_filter not in ('all',) + ONLINE_SOURCE_VALUES:
        online_source_filter = 'all'

    # Validate date range: if both ends are present and from > to, the query
    # would return nothing silently. Flash a warning and drop the bad bound
    # so the user sees the page with a clear signal instead of empty data.
    if date_from and date_to and date_from > date_to:
        flash('Invalid date range: "From" date is after "To" date. '
              'Please adjust the filter.', 'error')
        date_from = ''
        date_to = ''

    # The reports helper expects a combined "category|description" key.
    # Form: separate `category` + `description` params (autocomplete).
    if cat_param and desc_param:
        filter_val = f'{cat_param}|{desc_param}'
    elif cat_param and not desc_param:
        filter_val = f'{cat_param}|'
    else:
        filter_val = ''

    db = get_db()
    business_name = get_setting(db, 'business_name') or 'Waqar & Brothers'
    ctx = _compute_reports_data(db, business_name, date_from, date_to,
                                filter_val, type_filter)
    if online_source_filter != 'all':
        src_total = ctx['online_source_breakdown'].get(online_source_filter, 0)
        ctx['online_source_breakdown'] = {k: (src_total if k == online_source_filter else 0)
                                          for k in ONLINE_SOURCE_VALUES}
        ctx['total_online'] = src_total
        # Recompute cash/online ratios against the filtered online total so the
        # "Cash vs Online" card stays consistent with the selected source.
        cash_online_base = ctx.get('total_cash_recovery', 0) + src_total
        if cash_online_base > 0:
            ctx['cash_ratio_pct'] = round(
                (ctx.get('total_cash_recovery', 0) * 100.0) / cash_online_base, 1)
            ctx['online_ratio_pct'] = round(
                (src_total * 100.0) / cash_online_base, 1)
        else:
            ctx['cash_ratio_pct'] = 0
            ctx['online_ratio_pct'] = 0
    ctx['online_source_filter'] = online_source_filter
    # Pass parts back so the template can prefill the autocomplete input.
    if cat_param and desc_param:
        ctx['filter_category'] = cat_param
        ctx['filter_description'] = desc_param
    else:
        ctx['filter_category'] = cat_param
        ctx['filter_description'] = ''

    # --- Salesman filter + dropdown options ---
    # Dropdown options: every non-deleted salesman. We include inactive
    # salesmen with an "(inactive)" suffix so historical filtering still
    # works — a salesman may have been deactivated after collecting.
    sm_options = db.execute(
        'SELECT id, name, is_active FROM salesmen '
        'WHERE is_deleted = 0 ORDER BY is_active DESC, name COLLATE NOCASE'
    ).fetchall()
    ctx['all_salesmen'] = [
        {'id': r['id'], 'name': r['name'], 'is_active': bool(r['is_active'])}
        for r in sm_options
    ]
    ctx['salesman_id_filter'] = salesman_id_filter
    ctx['salesman_filter_info'] = None

    if salesman_id_filter > 0:
        sm_row = db.execute(
            'SELECT id, name, is_active FROM salesmen WHERE id = ? AND is_deleted = 0',
            (salesman_id_filter,)
        ).fetchone()
        if sm_row:
            ctx['salesman_filter_info'] = {
                'id': sm_row['id'],
                'name': sm_row['name'],
                'is_active': bool(sm_row['is_active']),
            }

            # Narrow Recovery columns in Daily Breakdown + Overview to ONLY
            # this salesman's collections. Closing balance (running balance)
            # is intentionally left at business-wide totals because that is
            # the cash position of the shop, not the salesman.
            sm_daily_sql = '''
                SELECT date, COALESCE(SUM(amount), 0) AS recovery
                FROM daily_entries
                WHERE is_deleted = 0
                  AND type = 'recovery'
                  AND salesman_id = ?
            '''
            sm_daily_params = [salesman_id_filter]
            if date_from:
                sm_daily_sql += ' AND date >= ?'
                sm_daily_params.append(date_from)
            if date_to:
                sm_daily_sql += ' AND date <= ?'
                sm_daily_params.append(date_to)
            sm_daily_sql += ' GROUP BY date'
            sm_by_date = {
                r['date']: int(r['recovery'] or 0)
                for r in db.execute(sm_daily_sql, sm_daily_params).fetchall()
            }
            new_total_recovery = sum(sm_by_date.values())

            for row in ctx.get('daily_data', []):
                d = row.get('date')
                rec = sm_by_date.get(d, 0)
                row['recovery'] = rec
                # Cash collected per day = that salesman's recovery − business
                # online total for the day. Since a salesman's recovery is
                # typically cash, this usually equals the salesman's recovery.
                # We intentionally keep the same math shape as the unfiltered
                # view so columns stay comparable.
                row['cash_recovery'] = rec - int(row.get('online', 0) or 0)

            ctx['total_recovery'] = new_total_recovery
            # Recompute profit/loss consistent with the narrowed recovery.
            # Business expense total does NOT depend on the salesman filter.
            ctx['profit_loss'] = new_total_recovery - int(
                ctx.get('total_business_expense', 0) or 0
            )

    # PDF picker defaults (current month/year, with a short year range).
    # Added on the per-request copy returned by the cached helper — the
    # cached dict itself is a shallow copy so this doesn't leak.
    today = date.today()
    ctx['pdf_default_month'] = today.month
    ctx['pdf_default_year'] = today.year
    ctx['pdf_years'] = list(range(today.year, today.year - 5, -1))

    return render_template('reports.html', **ctx)


