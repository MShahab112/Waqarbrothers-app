@app.route('/login', methods=['GET', 'POST'])
def login():
    """Show login form (GET) or process login (POST)."""
    if request.method == 'GET':
        return render_template('login.html')

    # POST — process the login
    username = request.form.get('username', '').strip()
    password = request.form.get('password', '')

    if not username or not password:
        flash('Please enter both username and password.', 'error')
        return render_template('login.html')

    db = get_db()

    # Look up the user
    user = db.execute('SELECT * FROM users WHERE username = ?',
                      (username,)).fetchone()

    if user is None:
        flash('Invalid username or password.', 'error')
        return render_template('login.html')

    # Check if the account is locked
    if user['locked_until']:
        locked_until = datetime.strptime(user['locked_until'],
                                         '%Y-%m-%d %H:%M:%S')
        if datetime.now() < locked_until:
            remaining = (locked_until - datetime.now()).seconds // 60 + 1
            flash(f'Account is locked. Try again in {remaining} minute(s).',
                  'error')
            return render_template('login.html')
        else:
            # Lock period expired — reset failed attempts
            db.execute('''UPDATE users
                          SET failed_attempts = 0, locked_until = NULL
                          WHERE id = ?''', (user['id'],))
            db.commit()
            # Re-fetch user after reset
            user = db.execute('SELECT * FROM users WHERE id = ?',
                              (user['id'],)).fetchone()

    # Check the password
    if not check_password_hash(user['password'], password):
        # Wrong password — increment failed attempts
        new_attempts = user['failed_attempts'] + 1

        if new_attempts >= 5:
            # Lock the account for 15 minutes
            lock_time = (datetime.now() + timedelta(minutes=15)).strftime(
                '%Y-%m-%d %H:%M:%S')
            db.execute('''UPDATE users
                          SET failed_attempts = ?, locked_until = ?
                          WHERE id = ?''',
                       (new_attempts, lock_time, user['id']))
            db.commit()
            flash('Too many wrong attempts. Account locked for 15 minutes.',
                  'error')
        else:
            db.execute('UPDATE users SET failed_attempts = ? WHERE id = ?',
                       (new_attempts, user['id']))
            db.commit()
            remaining = 5 - new_attempts
            flash(f'Invalid username or password. {remaining} attempt(s) left.',
                  'error')

        return render_template('login.html')

    # Password is correct — reset failed attempts and log in
    db.execute('''UPDATE users
                  SET failed_attempts = 0, locked_until = NULL
                  WHERE id = ?''', (user['id'],))
    db.commit()

    session['user_id'] = user['id']
    session['username'] = user['username']

    # If must_change_password is set, redirect to change password page
    if user['must_change_password'] == 1:
        return redirect(url_for('change_password'))

    return redirect(url_for('daily'))


