@app.route('/api/descriptions')
@login_required
def api_descriptions():
    """Return description suggestions for smart input.

    Two response shapes (preserves existing callers):
    - No ?q= → legacy: top-10 most frequent descriptions as plain strings
      (used by add_entry / daily / edit_entry datalists).
    - With ?q= → reports autocomplete: up to 10 {category, description, label}
      objects matching the query, sorted alphabetically.
    """
    db = get_db()
    q = request.args.get('q', '').strip()

    if not q:
        rows = execute_timed(db, '''
            SELECT TRIM(description) COLLATE NOCASE AS desc, COUNT(*) as freq
            FROM daily_entries
            WHERE is_deleted=0 AND TRIM(COALESCE(description, '')) != ''
            GROUP BY TRIM(description) COLLATE NOCASE
            ORDER BY freq DESC
            LIMIT 10
        ''').fetchall()
        return jsonify([r['desc'].title() for r in rows])

    like = f'%{q}%'
    rows = execute_timed(db, '''
        SELECT DISTINCT COALESCE(category, '') AS category,
                        TRIM(description)       AS description
        FROM daily_entries
        WHERE is_deleted=0
              AND TRIM(COALESCE(description, '')) != ''
              AND description LIKE ? COLLATE NOCASE
        ORDER BY description COLLATE NOCASE ASC
        LIMIT 10
    ''', (like,)).fetchall()

    results = []
    seen = set()
    for r in rows:
        cat = r['category'] or ''
        desc = r['description'].title()
        key = f'{cat}|{desc}'
        if key in seen:
            continue
        seen.add(key)
        cat_label = get_category_label(cat) or 'Other'
        results.append({
            'category': cat,
            'description': desc,
            'label': f'{desc} ({cat_label})',
        })
    return jsonify(results)


@app.route('/api/descriptions/top')
@login_required
def api_descriptions_top():
    """Top-10 (category, description) pairs by frequency.

    Used by the Reports autocomplete to show popular entries when the
    search input is empty/focused. Separate endpoint so the legacy
    /api/descriptions?q= behavior — and its plain-string no-query shape —
    stays untouched for add_entry / daily / edit_entry.
    """
    db = get_db()
    rows = execute_timed(db, '''
        SELECT COALESCE(category, '')                AS category,
               TRIM(description) COLLATE NOCASE      AS description,
               COUNT(*)                              AS freq
        FROM daily_entries
        WHERE is_deleted = 0
              AND TRIM(COALESCE(description, '')) != ''
        GROUP BY COALESCE(category, ''), TRIM(description) COLLATE NOCASE
        ORDER BY freq DESC
        LIMIT 10
    ''').fetchall()

    results = []
    for r in rows:
        cat = r['category'] or ''
        desc = r['description'].title()
        cat_label = get_category_label(cat) or 'Other'
        results.append({
            'category': cat,
            'description': desc,
            'label': f'{desc} ({cat_label})',
        })
    return jsonify(results)


