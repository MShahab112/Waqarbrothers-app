@app.route('/add-entry', methods=['POST'])
@login_required
def add_entry():
    """Save a new recovery or expense entry."""
    db = get_db()

    entry_type = request.form.get('type', '').strip()
    entry_date = request.form.get('date', '').strip()
    description = request.form.get('description', '').strip()
    amount_str = request.form.get('amount', '').strip()
    notes = request.form.get('notes', '').strip()
    payment_type = request.form.get('payment_type', 'cash').strip()
    salesman_id_raw = request.form.get('salesman_id', '').strip()
    category_raw = request.form.get('category', '').strip().lower()
    online_source = request.form.get('online_source', '').strip().lower()
    bank_name = request.form.get('bank_name', '').strip()

    # Validate payment_type (Cash / Online / Bank Slip)
    if payment_type not in ('cash', 'bank_slip'):
        payment_type = 'cash'

    # --- Resolve salesman for recovery entries ---
    # STRICT: the form MUST send a valid salesman_id that maps to an
    # existing, active salesman. We do NOT auto-create from typed text —
    # new salesmen must go through the PIN-protected creation flow at
    # /api/salesmen/create. This prevents silently bypassing the PIN by
    # just typing a new name and clicking Save.
    salesman_id = None
    if entry_type == 'recovery':
        sid = 0
        if salesman_id_raw:
            try:
                sid = int(salesman_id_raw)
            except (ValueError, TypeError):
                sid = 0
        if sid <= 0:
            flash('Please select or create a valid salesman first.', 'error')
            return redirect(url_for('daily', date=entry_date or date.today().isoformat()))

        sm_row = db.execute(
            'SELECT id, name, is_active FROM salesmen WHERE id = ?',
            (sid,)
        ).fetchone()
        if sm_row is None:
            flash('Please select or create a valid salesman first.', 'error')
            return redirect(url_for('daily', date=entry_date or date.today().isoformat()))
        if not sm_row['is_active']:
            flash('That salesman is inactive. Reactivate it first from the Salesmen page.',
                  'error')
            return redirect(url_for('daily', date=entry_date or date.today().isoformat()))

        salesman_id = sm_row['id']
        description = sm_row['name']

    # Category is user-selected for expenses — required, no silent fallback.
    # Recovery/online entries don't carry an expense category.
    if entry_type == 'expense':
        if category_raw not in VALID_CATEGORIES:
            flash('Please select a category for the expense.', 'error')
            return redirect(url_for('add_entry_form', type=entry_type,
                                    date=entry_date or date.today().isoformat()))
        category = category_raw
        # Normalize free-text description so grouping (esp. Advance per-person
        # totals) collapses casing/spacing variants into one bucket.
        description = normalize_expense_description(description)
    elif entry_type == 'online':
        if online_source not in ONLINE_SOURCE_VALUES:
            flash('Please select an online source.', 'error')
            return redirect(url_for('daily', date=entry_date or date.today().isoformat()))
        if online_source == 'bank_transfer' and not bank_name:
            flash('Bank name is required for bank transfer source.', 'error')
            return redirect(url_for('daily', date=entry_date or date.today().isoformat()))
        if online_source != 'bank_transfer':
            bank_name = ''
        category = None
        payment_type = 'online'
        source_label = ONLINE_SOURCE_LABELS.get(online_source, 'Online')
        description = source_label if not bank_name else f'{source_label} ({bank_name})'
    else:
        category = None
        online_source = None
        bank_name = ''

    # Handle receipt file upload (validated: jpg/jpeg/png, ≤5 MB, uuid name).
    # Always read from request.files in the SAME request — duplicate entries
    # are allowed through without any re-submit flow, so the file is never
    # lost on the way to the DB.
    try:
        receipt_filename = save_receipt_file(request.files.get('receipt'))
    except ValueError as exc:
        flash(str(exc), 'error')
        return redirect(url_for('daily', date=entry_date or date.today().isoformat()))

    # --- Validation ---

    # Type must be recovery, online, or expense
    if entry_type not in ('recovery', 'online', 'expense'):
        flash('Invalid entry type.', 'error')
        return redirect(url_for('daily'))

    # Date must be valid
    try:
        parsed_date = date.fromisoformat(entry_date)
    except ValueError:
        flash('Invalid date format.', 'error')
        return redirect(url_for('daily'))

    # Date must not be in the future
    if parsed_date > date.today():
        flash('Future dates are not allowed.', 'error')
        return redirect(url_for('add_entry_form', type=entry_type,
                                date=entry_date))

    # Block if the target day is already closed
    if is_day_closed(entry_date):
        flash('This day is closed. No changes allowed.', 'error')
        return redirect(url_for('daily', date=entry_date))

    # Description is required
    if not description:
        if entry_type == 'recovery':
            flash('Salesman name is required.', 'error')
        elif category == 'advance':
            flash('Person name is required for Advance entries.', 'error')
        else:
            flash('Name / Purpose is required.', 'error')
        return redirect(url_for('add_entry_form', type=entry_type,
                                date=entry_date))

    # Amount must be a positive whole number
    try:
        amount = int(amount_str)
    except (ValueError, TypeError):
        flash('Amount must be a valid number.', 'error')
        return redirect(url_for('add_entry_form', type=entry_type,
                                date=entry_date))

    if amount <= 0:
        flash('Amount must be greater than 0.', 'error')
        return redirect(url_for('add_entry_form', type=entry_type,
                                date=entry_date))

    # Data integrity guard: block saves that would drive closing_balance
    # below zero (an actual overdraft). Skipped for income entries (recovery,
    # online) because inflows never cause an overdraft. The check uses the
    # full closing-balance equation — opening + cash - expense - bank — so
    # yesterday's carry-over cash (opening balance) legitimately funds today's
    # outflow. The previous formulation (cash >= expense + bank) ignored the
    # opening balance and wrongly blocked small expenses on days with low
    # same-day recovery.
    if entry_type == 'expense':
        opening = get_opening_balance(db, entry_date)
        base = get_day_cash_outflow(db, entry_date)
        d_rec, d_onl, d_exp, d_bank = _classify_entry_deltas(
            entry_type, amount, category, payment_type
        )
        projected_cash = (base['recovery'] + d_rec) - (base['online'] + d_onl)
        projected_outflow = (base['expense'] + d_exp) + (base['bank_transfer'] + d_bank)
        projected_closing = opening + projected_cash - projected_outflow
        if projected_closing < 0:
            flash('Closing balance would go negative (overdraft).', 'error')
            return redirect(url_for('daily', date=entry_date))

    # --- Duplicate notice (informational only) ---
    # Same date + type + amount + description (+ category for expenses)
    # within the last 60s is flagged, but NEVER blocks the save. Duplicate
    # entries are allowed — we just log a flash notice so the user is aware.
    # This avoids the re-submit flow that used to drop the uploaded receipt
    # file. created_at is written in UTC so the window is DST-safe.
    now_str = utc_now_str()
    dup_sql = '''
        SELECT id
        FROM daily_entries
        WHERE is_deleted = 0
          AND date = ? AND type = ? AND amount = ?
          AND LOWER(TRIM(description)) = LOWER(TRIM(?))
          AND ABS(strftime('%s', COALESCE(NULLIF(created_at, ''), ?))
                - strftime('%s', ?)) < 60
    '''
    dup_params = [entry_date, entry_type, amount, description, now_str, now_str]
    if entry_type == 'expense':
        dup_sql += ' AND category IS ?'
        dup_params.append(category)
    duplicate_match = db.execute(dup_sql, dup_params).fetchone()
    if duplicate_match:
        app.logger.info(
            'Duplicate entry saved anyway | type=%s date=%s amount=%s description=%r',
            entry_type, entry_date, amount, description,
        )
        flash('A similar entry was saved just now — both have been kept.', 'warning')

    # --- Save the entry ---
    # UTC so the duplicate window comparison stays consistent no matter
    # what the host clock or timezone does.
    created_at = utc_now_str()
    db.execute('''
        INSERT INTO daily_entries (date, type, amount, description, notes,
                                   is_deleted, created_at, payment_type, receipt,
                                   category, salesman_id, online_source, bank_name)
        VALUES (?, ?, ?, ?, ?, 0, ?, ?, ?, ?, ?, ?, ?)
    ''', (entry_date, entry_type, amount, description, notes, created_at,
          payment_type, receipt_filename, category, salesman_id, online_source, bank_name))
    db.commit()
    _reports_cache.clear()

    app.logger.info(
        'Entry added | type=%s date=%s category=%s amount=%s description=%r | %s',
        entry_type, entry_date, category or '-', amount, description, _log_ctx(),
    )
    flash(f'{entry_type.capitalize()} entry saved.', 'success')
    return redirect(url_for('daily', date=entry_date))


@app.route('/api/check-duplicate')
@login_required
def api_check_duplicate():
    """Lightweight duplicate pre-check used by the Add Entry forms.

    JSON: {duplicate: bool}. Match is same date + type + amount +
    case-insensitive trimmed description on a non-deleted row.
    """
    db = get_db()
    entry_date = request.args.get('date', '').strip()
    entry_type = request.args.get('type', '').strip()
    description = request.args.get('description', '').strip()
    amount_str = request.args.get('amount', '').strip()

    if entry_type not in ('recovery', 'online', 'expense'):
        return {'duplicate': False}
    try:
        amount = int(amount_str)
    except (ValueError, TypeError):
        return {'duplicate': False}
    if not entry_date or not description or amount <= 0:
        return {'duplicate': False}

    if entry_type == 'recovery':
        description = ' '.join(description.split()).title()

    row = db.execute('''
        SELECT id FROM daily_entries
        WHERE is_deleted = 0
          AND date = ? AND type = ? AND amount = ?
          AND LOWER(TRIM(description)) = LOWER(TRIM(?))
        LIMIT 1
    ''', (entry_date, entry_type, amount, description)).fetchone()
    return {'duplicate': row is not None}


